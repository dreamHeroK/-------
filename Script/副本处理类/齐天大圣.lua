    if 玩家数据[队长id].队伍 == 0 or 玩家数据[队长id].队长 == false then
        常规提示(队长id, "#Y该任务必须组队完成且由队长操作")
        return
    end
    -- 校验队伍人数
    if 取队伍人数(队长id) < 人数 then
        常规提示(队长id, "#Y此副本要求队伍人数不低于"..人数.."人")
        return
    end
    -- 校验角色等级（最低60级）
    if 取等级要求(队长id, 60) == false then
        常规提示(队长id, "#Y此副本要求角色等级不能低于60级")
        return
    end
    -- 校验每日次数限制（队员是否已完成）
    if self:取齐天大圣次数限制(队长id) then return end
    -- 校验队员是否已有该副本任务
    if 取队员任务存在(队伍id, 940) then
        常规提示(队长id, "#Y队伍中已有队员正在进行该副本，无法重复操作")
        return
    end

    -- 2. 初始化副本基础数据（支撑奖励发放与数据清理）
    local 队伍成员列表 = 队伍数据[队伍id].成员数据  -- 所有队员ID
    local 主任务id = 队长id.."_940_"..os.time().."_"..随机序列.."_"..取随机数(1,999)  -- 生成唯一主任务ID
    local 副本id = 队长id  -- 用队长ID关联副本进程
    -- 初始化副本进程数据（仅用于奖励函数关联，跳过实际进程切换）
    副本数据.齐天大圣.进行[副本id] = {
        进程 = 1,
        主任务id = 主任务id,
        小猴老猴 = {伤心的小猴=0,临死的老猴=0},  -- 初始化计数避免奖励函数报错
        无常 = {黑无常=0,白无常=0},
        展示实力 = {玉皇大帝=0,四大天王=0},
        盗马贼 = 0,
        百万天兵 = {百万天兵=0,巨灵神=0}
    }
    -- 初始化主任务数据（与原逻辑一致，用于后续清理）
    任务数据[主任务id] = {
        id = 主任务id,
        起始 = os.time(),
        结束 = 7200,  -- 副本超时时间（2小时，与原逻辑一致）
        队伍组 = 队伍成员列表,
        副本id = 副本id,
        类型 = 940
    }

    local 所有奖励类型 = {
        941,        -- 进程1：伤心的小猴（对话奖励）
        941.1,      -- 进程1：临死的老猴（对话奖励）
        155590,     -- 进程2：白无常（战斗奖励）
        155591,     -- 进程2：黑无常（战斗奖励）
        944,        -- 进程3：判官（对话奖励）
        155592,     -- 进程4：阎王（战斗奖励）
        946,        -- 进程5：太白金星（对话奖励）
        155598,     -- 进程6：四大天王（战斗奖励）
        155593,     -- 进程6：玉皇大帝（战斗奖励）
        949,        -- 进程7：玉皇大帝授封（对话奖励）
        155594,     -- 进程8：盗马贼（战斗奖励）
        951,        -- 进程9：调皮的小马（对话奖励）
        952,        -- 进程10：天蓬元帅（对话奖励）
        155595,     -- 进程11：百万天兵（战斗奖励）
        155596,     -- 进程11：巨灵神（战斗奖励）
        953,        -- 进程11：李靖（对话奖励）
        155597      -- 进程12：镇塔之神（最终战斗奖励）
    }

    for _, 奖励类型 in ipairs(所有奖励类型) do
        完成齐天大圣任务(队伍成员列表, 主任务id, 奖励类型, 倍率, true)
    end

    -- 4. 数据清理与状态更新（标记副本完成，重置玩家状态）
    for _, 临时id in ipairs(队伍成员列表) do
        if 玩家数据[临时id] then
            副本数据.齐天大圣.完成[临时id] = true
            更新玩家每日(临时id, "副本任务", "齐天大圣")
            if 玩家数据[临时id] and 玩家数据[临时id].连接id then
                  发送数据(玩家数据[临时id].连接id, 550.3, {齐天大圣=true})
            end
            -- 玩家数据[临时id].角色:取消任务(940)
            -- 玩家数据[临时id].角色:刷新任务跟踪()
            常规提示(临时id, "#Y恭喜！已直接完成齐天大圣副本，获得所有奖励")
        end
    end

end

