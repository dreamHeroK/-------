       	常规提示(队长id,"#Y/扫荡副本需消耗1个扫荡卡")
    	return
    end
    玩家数据[队长id].道具:消耗背包道具(玩家数据[队长id].连接id,队长id,"扫荡卡",1)

     local 倍率 =0.8
    if not 玩家数据[队长id] then
        常规提示(队长id, "#R/无效队长id，无法完成副本")
        return
    end
    local 队伍ID = 玩家数据[队长id].队伍
    if 队伍ID == 0 then
        常规提示(队长id, "#Y/请组队后操作")
        return
    end
    local 副本ID = 队长id
    local 队伍成员列表 = 队伍数据[队伍ID].成员数据
    for _, 成员ID in ipairs(队伍成员列表) do
        if 副本数据.水陆大会.完成[成员ID] then
            常规提示(队长id, "#Y"..玩家数据[成员ID].角色.名称.."本日已完成副本，无法参与")
            return
        end
    end
    副本数据.水陆大会.进行[副本ID] = {
        进程 = 1,
        数量 = 10,
        序列 = {}
    }
    -- 临时战斗数据（仅含副本/任务必要字段，无玩家地图关联）
    local 临时战斗数据 = {
        任务id = "temp_"..副本ID.."_600",
        副本id = 副本ID,
        地图编号 = 6003, -- 固定用初始地图编号（无需切换，仅供胜利函数识别副本场景）
