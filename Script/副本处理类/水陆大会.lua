    -- 进程3→4：玄奘复命→找长安兵长（删除地图跳转代码）
    -- --------------------------
    副本数据.水陆大会.进行[副本ID].进程 = 4 -- 仅推进进程，不改变玩家地图

    -- --------------------------
    -- 进程4→5：清理10个前锋营阴魂
    -- --------------------------
    任务数据[临时战斗数据.任务id] = 临时战斗数据
    for i = 1, 10 do
        副本数据.水陆大会.进行[副本ID].数量 = 10 - i
        胜利MOB_100057(队长id, 临时战斗数据, 队伍成员列表,倍率,true)
    end
    副本数据.水陆大会.进行[副本ID].进程 = 6
    任务处理类:删除单位(临时战斗数据.任务id)
    任务数据[临时战斗数据.任务id] = nil

