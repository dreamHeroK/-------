function 副本_车迟斗法:扫荡(队长id)
    if not 玩家数据[队长id].道具:判定背包道具(队长id,"扫荡卡",1) then
       	常规提示(队长id,"#Y/扫荡副本需消耗1个扫荡卡")
    	return
    end
    玩家数据[队长id].道具:消耗背包道具(玩家数据[队长id].连接id,队长id,"扫荡卡",1)

    local 副本等级 = 50
    local 人数 = 5
    if 调试模式 then 人数 = 1 end -- 调试模式支持1人
    local 队伍id = 玩家数据[队长id].队伍

    -- 校验队长身份与队伍状态
    if 玩家数据[队长id].队伍 == 0 or 玩家数据[队长id].队长 == false then
        常规提示(队长id, "#Y该任务必须组队完成且由队长操作")
        return
    end
    -- 校验队伍人数
    if 取队伍人数(队长id) < 人数 then
        常规提示(队长id, "#Y此副本要求队伍人数不低于"..人数.."人")
        return
    end
    -- 校验等级（最低50级）
    if 取等级要求(队长id, 副本等级) == false then
        常规提示(队长id, "#Y此副本要求角色等级不能低于"..副本等级.."级")
        return
    end
    -- 校验队员是否已完成或有其他副本任务
    for n=1,#队伍数据[队伍id].成员数据 do
        local 临时id = 队伍数据[队伍id].成员数据[n]
        if 副本数据.车迟斗法.完成[临时id] ~= nil then
            常规提示(队长id, "#Y"..玩家数据[临时id].角色.名称.."本日已经完成过此副本了")
            return
        elseif 玩家数据[临时id].角色:取任务(610) ~= 0 then
            常规提示(队长id, "#Y"..玩家数据[临时id].角色.名称.."正在进行副本任务，无法领取新的副本")
            return
        end
    end

    -- 2. 初始化副本数据（模拟正常开启流程，支撑后续奖励发放）
    local 队伍成员列表 = 队伍数据[队伍id].成员数据
    local 副本id = 队长id
    local 主任务id = 副本id.."_610_"..os.time().."_"..随机序列.."_"..取随机数(88,99999999)

    -- 初始化副本进程数据（直接设为最终进程7，跳过中间环节）
    副本数据.车迟斗法.进行[副本id] = {
        进程 = 7,
        数量 = 0 -- 临时赋值，避免函数报错
    }
    -- 初始化主任务数据（符合原数据结构）
    任务数据[主任务id] = {
        副本名称 = "车迟斗法",
        id = 主任务id,
        起始 = os.time(),
        结束 = 7200,
        销毁 = true,
        副本重置 = true,
        玩家id = 0,
        队伍组 = table.loadstring(table.tostring(队伍成员列表)),
        副本id = 副本id,
        类型 = 610
    }
    local 所有战斗类型 = {100101, 100102, 100103, 100104} -- 供品/三清/雷公/三力大仙
    for _, 战斗类型 in ipairs(所有战斗类型) do
        -- 模拟战斗数据（满足胜利奖励函数的参数要求）
        local 模拟战斗数据 = {
            任务id = 主任务id,
            进入战斗玩家id = 队长id,
            副本id = 副本id
        }
        -- 根据战斗类型调用对应胜利函数
        local 倍率=0.8
        if 战斗类型 == 100101 then
            胜利MOB_100101(队长id, 模拟战斗数据, 队伍成员列表,倍率)
        elseif 战斗类型 == 100102 then
            胜利MOB_100102(队长id, 模拟战斗数据, 队伍成员列表,倍率,true)
        elseif 战斗类型 == 100103 then
            胜利MOB_100103(队长id, 模拟战斗数据, 队伍成员列表,倍率,true)
        elseif 战斗类型 == 100104 then
            胜利MOB_100104(队长id, 模拟战斗数据, 队伍成员列表,倍率,true)
        end
    end

    -- 3.2 发放宝箱奖励（进程7的个人宝箱+小宝箱）
    for _, 临时id in ipairs(队伍成员列表) do
        -- 个人专属宝箱奖励（对应类型620）
        战斗奖励:其他奖励(临时id, "车迟宝箱")
        -- 公共小宝箱奖励（对应类型621，每人1份）
        战斗奖励:其他奖励(临时id, "车迟小宝箱")
        常规提示(临时id, "#Y已获得车迟宝箱和车迟小宝箱奖励")
    end

    for _, 临时id in ipairs(队伍成员列表) do
        副本数据.车迟斗法.完成[临时id] = true
        更新玩家每日(临时id, "副本任务", "车迟斗法")
        if 玩家数据[临时id] and 玩家数据[临时id].连接id then
            发送数据(玩家数据[临时id].连接id, 550.3,{车迟斗法=true})
        end
        常规提示(临时id, "#Y恭喜！已直接完成车迟斗法副本，获得所有奖励！")
    end
end

