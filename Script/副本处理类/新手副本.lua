function 副本_新手副本:扫荡(队长id)
    if not 玩家数据[队长id].道具:判定背包道具(队长id,"扫荡卡",1) then
       	常规提示(队长id,"#Y/扫荡副本需消耗1个扫荡卡")
    	return
    end
    玩家数据[队长id].道具:消耗背包道具(玩家数据[队长id].连接id,队长id,"扫荡卡",1)

    local 倍率=0.8
    local 最低等级 = 30          -- 原副本等级要求
    local 非调试最小人数 = 5     -- 非调试模式最低人数
    local 所有战斗类型 = {102000,102001,102002,102003,102004,102005,102006,102007,102008} -- 全阶段战斗类型
    if not 玩家数据[队长id] or 玩家数据[队长id].队长 ~= true then
        常规提示(队长id,"#Y请先组建队伍！"); return
    end
    local 队伍id = 玩家数据[队长id].队伍
    if 队伍id ==0 then
        常规提示(队长id,"#Y请先组建队伍！"); return
    end
    local 队员列表 = 队伍数据[队伍id].成员数据
    if #队员列表 < 非调试最小人数 and 调试模式 == false then
        常规提示(队长id,"#Y非调试模式需5人队伍！"); return
    end
    for _,队员id in ipairs(队员列表) do
        if 玩家数据[队员id] and 玩家数据[队员id].角色 and 玩家数据[队员id].角色.等级 < 最低等级 then
            常规提示(队长id,"#Y"..(玩家数据[队员id].角色.名称 or "未知玩家").."等级不足"..最低等级.."级！"); return
        end
        if 副本数据.新手副本.完成[队员id] then
            常规提示(队长id,"#Y"..(玩家数据[队员id].角色.名称 or "未知玩家").."今日已完成该副本！"); return
        end
        if 玩家数据[队员id] and 玩家数据[队员id].角色 and 玩家数据[队员id].角色:取任务(400) ~= 0 then
            常规提示(队长id,"#Y"..(玩家数据[队员id].角色.名称 or "未知玩家").."正在其他新手副本中！"); return
        end
    end
    for _, 战斗类型 in ipairs (所有战斗类型) do
        self:完成奖励(队员列表, 战斗类型,倍率)
    end
    for _,队员id in ipairs(队员列表) do
        if 玩家数据[队员id] and 玩家数据[队员id].角色 then
            副本数据.新手副本.完成[队员id] = true
            if 玩家数据[队员id] and 玩家数据[队员id].连接id then
                  发送数据(玩家数据[队员id].连接id, 550.3,{新手副本=true})
            end
            常规提示(队员id,"#Y新手副本（初出茅庐）已直接完成，所有奖励已发放！")
        end
    end
end
return 副本_新手副本
