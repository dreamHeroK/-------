function 副本_秘境降妖:扫荡(队长id)
    if not 玩家数据[队长id].道具:判定背包道具(队长id,"扫荡卡",1) then
        常规提示(队长id,"#Y/扫荡副本需消耗1个扫荡卡")
      return
    end
    玩家数据[队长id].道具:消耗背包道具(玩家数据[队长id].连接id,队长id,"扫荡卡",1)

    local 倍率=0.8
    local 人数 = 5
    if 调试模式 then 人数 = 1 end -- 调试模式支持1人
    local 队伍id = 玩家数据[队长id].队伍
    if 玩家数据[队长id].队伍 == 0 or 玩家数据[队长id].队长 == false then
        常规提示(队长id, "#Y该任务必须组队完成且由队长操作")
        return
    end
    -- 校验队伍人数
    if 取队伍人数(队长id) < 人数 then
        常规提示(队长id, "#Y此副本要求队伍人数不低于"..人数.."人")
        return
    end
    -- 校验等级（60级最低要求）
    if 取等级要求(队长id, 60) == false then
        常规提示(队长id, "#Y此副本要求角色等级不能低于60级")
        return
    end
    -- 校验次数限制（队员是否已完成）
    if self:取秘境降妖次数限制(队长id) then return end
    -- 校验队员是否已有副本任务
    if 取队员任务存在(队伍id, 90) then
        常规提示(队长id, "#Y队伍中已有队员正在进行该副本，无法重复操作")
        return
    end

    -- 2. 初始化副本基础数据（支撑奖励发放与数据清理）
    local 队伍成员列表 = 队伍数据[队伍id].成员数据
    local 主任务id = 队长id.."_90_"..os.time().."_"..随机序列.."_"..取随机数(1,999)
    local 副本id = 队长id

    -- 初始化副本进程数据
    副本数据.秘境降妖.进行[副本id] = {
        进程 = 1,
        主任务id = 主任务id,
        数量 = 1 -- 临时赋值，避免奖励函数报错
    }
    -- 初始化主任务数据
    任务数据[主任务id] = {
        id = 主任务id,
        起始 = os.time(),
        结束 = 7200,
        队伍组 = 队伍成员列表,
        副本id = 副本id,
        类型 = 90
    }


    -- 3. 核心：发放所有阶段战斗奖励（遍历所有战斗类型，调用原奖励函数）
    local 所有战斗类型 = {
        155619, -- 一层-特处士兵
        155620, -- 二层-偏将军
        155621, -- 三层-战败者
        155622, -- 四层-秘境魔头
        155623, -- 五层-白衣秀士
        155624, -- 六层-武器箱
        155625, -- 七层-秘境老妖
        155629, -- 八层-唐王（继续前进）
        155626, -- 九层-秘境牛魔
        155627, -- 十层-秘境圣子
        155630, -- 八层-唐王（首领挑战）
        155628  -- 十一层-秘境妖王（最终）
    }
    -- 遍历战斗类型，为每个队员发放所有奖励
    for _, 战斗类型 in ipairs(所有战斗类型) do
        -- 调用原奖励函数，传入队伍成员列表、临时任务ID、战斗类型
        self:完成秘境降妖任务(队伍成员列表, 主任务id, 战斗类型,倍率,true)
    end

    for _, 临时id in ipairs(队伍成员列表) do
        副本数据.秘境降妖.完成[临时id] = true
        更新玩家每日(临时id, "副本任务", "秘境降妖")
        if 玩家数据[临时id] and 玩家数据[临时id].连接id then
            发送数据(玩家数据[临时id].连接id, 550.3,{秘境降妖=true})
        end
        常规提示(临时id, "#Y恭喜！已直接完成秘境降妖副本，获得所有阶段奖励！")
    end
end

