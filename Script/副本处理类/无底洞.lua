        队伍组 = 队员列表,
        类型 = 980,
        销毁 = true,
        副本重置 = true
    }
    -- 3.2 初始化副本进程与计数（避免奖励函数因状态缺失报错）
    副本数据.无底洞.进行[副本标识id] = {
        进程 = 17,  -- 直接拉满至最终进程（对应原副本最后阶段）
        主任务id = 主任务id,
        蟊贼 = 10,  -- 满足进程2的击杀要求（需10个）
