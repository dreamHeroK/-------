        常规提示(队长id,"#Y/扫荡副本需消耗1个扫荡卡")
      return
    end
    玩家数据[队长id].道具:消耗背包道具(玩家数据[队长id].连接id,队长id,"扫荡卡",1)

    local 倍率=0.8
    local 最低等级 = 60                  -- 原副本等级要求
    local 非调试最小人数 = 5             -- 非调试模式最低队伍人数
    local 调试最小人数 = 1               -- 调试模式最低人数
    local 所有战斗类型 = {               -- 全阶段战斗类型（奖励来源）
        155609, 155610, 155611, 155612,
        155613, 155614, 155616, 155617, 155618
    }
    local 副本积分奖励 = 200             -- 原副本完成固定积分
    if not 玩家数据[队长id] or 玩家数据[队长id].队长 ~= true then
        常规提示(队长id,"#Y仅副本队长可触发直接完成！"); return
    end
    local 队伍id = 玩家数据[队长id].队伍
    if 队伍id==0 then
        常规提示(队长id,"#Y请先组建有效队伍！"); return
    end
    local 队员列表 = 队伍数据[队伍id].成员数据
    local 当前所需人数 = 调试模式 and 调试最小人数 or 非调试最小人数

    -- 2.2 人数、等级、每日次数校验
    if #队员列表 < 当前所需人数 then
        常规提示(队长id,"#Y此副本要求队伍人数不低于"..当前所需人数.."人（调试模式除外）"); return
    end
    for _,队员id in ipairs(队员列表) do
        -- 等级校验
        if 玩家数据[队员id] and 玩家数据[队员id].角色 and 玩家数据[队员id].角色.等级 < 最低等级 then
