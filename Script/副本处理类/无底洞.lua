    }
    for _,战斗类型 in ipairs(所有战斗类型) do
        local 临时战斗任务id = "temp_wudidong_battle_"..战斗类型.."_"..os.time()
        任务数据[临时战斗任务id] = {
            id = 临时战斗任务id,
