        if 副本数据.无底洞.完成[队员id] then
            常规提示(队长id,"#Y"..(玩家数据[队员id].角色.名称 or "未知玩家").."今日已完成该副本！"); return
        end
        -- 队员是否在其他无底洞副本中
        if 玩家数据[队员id] and 玩家数据[队员id].角色 and 玩家数据[队员id].角色:取任务(980) ~= 0 then
            常规提示(队长id,"#Y"..(玩家数据[队员id].角色.名称 or "未知玩家").."正在其他无底洞副本中！"); return
        end
    end

    -- 3. 初始化副本最小状态（支撑奖励发放与收尾，不创建冗余单位）
    local 副本标识id = 队长id
    -- 3.1 初始化主任务数据（原副本核心依赖，类型980）
