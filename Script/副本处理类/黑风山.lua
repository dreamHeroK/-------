function 副本_黑风山:扫荡(队长id)
    if not 玩家数据[队长id].道具:判定背包道具(队长id,"扫荡卡",1) then
        常规提示(队长id,"#Y/扫荡副本需消耗1个扫荡卡")
      return
    end
    玩家数据[队长id].道具:消耗背包道具(玩家数据[队长id].连接id,队长id,"扫荡卡",1)

    local 副本等级 = 60
    local 人数 = 5
    if 调试模式 then 人数 = 1 end -- 调试模式支持1人
    local 队伍id = 玩家数据[队长id].队伍

    -- 校验队长身份
    if 玩家数据[队长id].队伍 == 0 or 玩家数据[队长id].队长 == false then
        常规提示(队长id, "#Y该任务必须组队完成且由队长操作")
        return
    end
    -- 校验队伍人数
    if 取队伍人数(队长id) < 人数 and not 调试模式 then
        常规提示(队长id, "#Y此副本要求队伍人数不低于"..人数.."人")
        return
    end
    -- 校验等级（最低60级）
    if 取等级要求(队长id, 副本等级) == false then
        常规提示(队长id, "#Y此副本要求角色等级不能低于"..副本等级.."级")
        return
    end
    -- 校验次数限制（当日未完成）
    if self:取黑风山次数限制(队长id) then
        return
    end
    -- 校验队员无其他副本任务
    if 取队员任务存在(队伍id, 80) then
        常规提示(队长id, "#Y队伍中已有队员正在进行该副本，无法直接完成")
        return
    end

    -- 2. 初始化副本核心数据（支撑奖励发放函数正常调用）
    local 队伍成员列表 = 队伍数据[队伍id].成员数据
    local 副本id = 队长id
    local 主任务id = 副本id.."_80_"..os.time().."_"..随机序列.."_"..取随机数(1,999)

    -- 初始化副本进程为最终状态，跳过所有中间环节
    副本数据.黑风山.进行[副本id] = {
        进程 = 8,
        协助任务 = {镇山太保=0, 婆婆=0, 唐玄奘=0}, -- 标记前置任务完成
        数量 = 0, -- 标记怪物/任务数量达标
        主任务id = 主任务id
    }
    任务数据[主任务id] = {
        id = 主任务id,
        起始 = os.time(),
        结束 = 7200,
        玩家id = {},
        队伍组 = 队伍成员列表,
        副本id = 副本id,
        类型 = 80
    }

    -- 3. 定义所有关键战斗类型（覆盖副本全奖励场景，无遗漏）
    local 关键战斗类型 = {
        81,          -- 黑风山打扫（婆婆协助任务）
        82,          -- 黑风山答题（唐玄奘协助任务）
        155066,      -- 黑风山巡逻（镇山太保协助任务）
        155067,      -- 绝望的牛头等怪物（进程2）
        155068,      -- 恶虎（进程3）
        155069,      -- 鬼鬼祟祟的黑熊精（进程4）
        155071,      -- 熊熊烈火/星星火光（进程5）
        155072,      -- 黑汉（进程6）
        155073,      -- 黑熊精队长（进程7）
        155074       -- 最终BOSS黑熊精（含特效宝珠奖励）
    }
    local 倍率 = 0.8 -- 奖励倍率（可根据需求调整，避免过高）

    -- 4. 发放所有战斗/任务奖励（调用原奖励函数，确保奖励一致性）
    for _, 战斗类型 in ipairs(关键战斗类型) do
        self:完成黑风山任务(队伍成员列表, 主任务id, 战斗类型, 倍率,true)
    end

    -- 5. 副本状态闭环（标记完成+清理数据+传送玩家）
    for _, 临时id in ipairs(队伍成员列表) do
        副本数据.黑风山.完成[临时id] = true
        更新玩家每日(临时id, "副本任务", "黑风山")
        玩家数据[临时id].角色:添加副本积分(200)
        玩家数据[临时id].角色:取消任务(主任务id)
        常规提示(临时id, "#Y恭喜！已直接完成黑风山副本，所有奖励已发放！")
         if 玩家数据[临时id] and 玩家数据[临时id].连接id then
            发送数据(玩家数据[临时id].连接id, 550.3,{黑风山=true})
        end

    end

    -- 清理副本临时数据，避免内存占用
    副本数据.黑风山.进行[副本id] = nil
    任务数据[主任务id] = nil
end

