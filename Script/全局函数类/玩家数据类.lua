function 添加累充(id,数额,提示,每日)
   	if  玩家数据[id].角色.累充金额==nil then
  		 玩家数据[id].角色.累充金额=0
  	end
  	local 数额=tonumber(数额)
  	if 数额 then
  		玩家数据[id].角色.累充金额=玩家数据[id].角色.累充金额+数额
  		常规提示(id,"#Y恭喜您获得累计充值额度:"..数额..",当前总累计充值额度："..玩家数据[id].角色.累充金额)
  		if 每日充值[id] == nil then 每日充值[id] = {当前=0} end
  		if 每日 then
	    	每日充值[id].当前 = 每日充值[id].当前 + 数额
	    常规提示(id,"#Y恭喜您获得每日充值额度:"..数额..",当前总每日充值额度："..每日充值[id].当前)
	    end
	   local 代理= "代理"..f函数.读配置(程序目录..[[data\]]..玩家数据[id].账号..[[\账号信息.txt]],"账号配置","邀请码")--玩家数据[id].角色.邀请码
	   代理充值[代理]= 代理充值[代理] or {注册={},充值={}}
	   代理充值[代理].充值[#代理充值[代理].充值+1]={数额=数额,时间=os.time(),账号=玩家数据[id].账号,id=id,名称=玩家数据[id].角色.名称}
	   代理充值[代理].充值.累计=(代理充值[代理].充值.累计 or 0) +数额
	   代理充值[代理].充值.未结算=(代理充值[代理].充值.未结算 or 0) +数额
	   if not 代理充值[代理].充值.记录 or 代理充值[代理].充值.记录~=math.floor(os.time()/86400) then
	   	代理充值[代理].充值.记录=math.floor(os.time()/86400)
	   	代理充值[代理].充值.今日=0
	   end
	   	代理充值[代理].充值.今日=代理充值[代理].充值.今日+数额
  	else
  		常规提示(id,"#Y数值非法！")
  	end
end
