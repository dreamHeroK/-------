function 重置抽奖池物品数量(id,当前抽奖数据, 配置文件路径, 玩家抽奖数据, 是否返还次数)
    for _, 物品 in ipairs(当前抽奖数据) do
        if 物品 and 物品.初始剩余 then  -- 只处理有初始值的物品
            物品.剩余数量 = 物品.初始剩余  -- 恢复初始剩余数量
        end
    end
       写出文件(配置文件路径, formatTable(当前抽奖数据))
    if 是否返还次数 and 玩家抽奖数据 then
        玩家抽奖数据.次数 = 玩家抽奖数据.次数 + 1  -- 返还1次
        常规提示(id, "#Y已自动重置并返还1次抽奖次数")
        广播消息({内容=format("#G/[抽奖奖池]#Y/奖池已经自动进行了更新".."#"..取随机数(1,110)),频道="xt"})
        发送公告("#P/[抽奖奖池]#Y/奖池已经自动进行了更新！#51")

    end
end

