function 加载账号仓库数据(账号,数字id)
    	if 账号仓库数据[账号] == nil then
	        	账号仓库数据[账号]={}
	        	账号仓库数据[账号] = 账号仓库类:创建(账号)
	        	账号仓库数据[账号]:加载数据(账号, 数字id)
    	else
		账号仓库数据[账号]={}
	        	账号仓库数据[账号] = 账号仓库类:创建(账号)
	        	账号仓库数据[账号]:加载数据(账号, 数字id)
    	end
end

