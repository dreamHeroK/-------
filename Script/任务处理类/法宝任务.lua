function 法宝任务:完成法宝任务(id,任务id)
  if 任务数据[任务id]==nil then
    常规提示(id,"#Y/你没有这样的任务")
    return
  end
  玩家数据[id].道具:给予道具(id,任务数据[任务id].材料名称,1,"不存共享")
  常规提示(id,"#Y/你获得了"..任务数据[任务id].材料名称)
  添加最后对话(id,"看你人还不错，这件宝贝就送给你吧！")
  玩家数据[id].角色:取消任务(任务id)
  任务数据[任务id]=nil
end


function 法宝任务:法宝内丹任务(id)
