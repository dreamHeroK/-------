    常规提示(id,"#Y/完成此任务必须达到60级")
    return
  end
  local 任务id=id.."_41_"..os.time().."_"..随机序列.."_"..取随机数(1,999)
  local 地图范围={1501,1193,1506,1110,1514,1091,1174,1173}
  local 地图=地图范围[取随机数(1,#地图范围)]
  local 随机参数=取随机数()
  local 模型=取随机怪(65,155)[2]
  local 名称 = "千年老妖"
  local xy=地图处理类.地图坐标[地图]:取随机点()
  任务数据[任务id]={
    id=任务id,
    起始=os.time(),
    结束=3600,
    玩家id={},
    队伍组={},
    名称=名称,
    模型=模型,
    x=xy.x,
    y=xy.y,
    地图编号=地图,
    地图名称=取地图名称(地图),
    类型=41
  }
  地图处理类:添加单位(任务id)
  玩家数据[id].角色:添加任务(任务id)
  添加最后对话(id,format("听说#Y/%s#W/正在#G/%s(%s,%s)#W/处作恶，你速速前往斩杀取回其内丹。",任务数据[任务id].名称,任务数据[任务id].地图名称,任务数据[任务id].x,任务数据[任务id].y))
end


