function 五行年兽:五行年兽任务(id,类型)

  if 玩家数据[id].角色:取任务(58) ~= 0 then
    添加最后对话(id,"少侠身上已经有此类任务，请先完成任务再来！")
    return
  elseif 玩家数据[id].队伍 == 0 then
    常规提示(id,"#Y/本任务至少需要1人组队完成")
    return
  elseif 取队伍人数(id)<1 then
    常规提示(id,"#Y/本任务至少需要2人组队完成")
    return
  elseif 取队员任务存在(玩家数据[id].队伍,58) then
    常规提示(id,"#Y/队伍中已有队员领取过此任务了")
    return
  elseif 五行年兽限制[id] then
    常规提示(id,"#Y/你今日已经参与过此活动了")
    return
  end
  local 队伍id=玩家数据[id].队伍
  for n=2,#队伍数据[队伍id].成员数据 do
    local 临时id=队伍数据[队伍id].成员数据[n]
    if  五行年兽限制[临时id] then
      常规提示(id,"#Y/"..玩家数据[临时id].角色.名称.."今日已经参与过此任务了")
      return
    end
  end
  local 序列=取随机数(1,#Q_随机人物)
  local 地图=Q_随机人物[序列].地图
  local 名称=Q_随机人物[序列].人物[取随机数(1,#Q_随机人物[序列].人物)]
  local 任务id=id.."_58_"..随机序列..os.time()
  任务数据[任务id]={
    id=任务id,
    起始=os.time(),
    结束=9999999,
    玩家id=id,
    队伍组={},
    难度=类型,
    环数=1,
    --关键字="完成五行年兽战斗任务",
    门派师傅=玩家数据[id].最后对话.名称,
    地图编号=地图,
    名称=名称,
    进程=1,
    类型=58,
    积分=0,
  }
  self:五行年兽任务物品(id,任务id)
  local 队伍id=玩家数据[id].队伍
  for n=1,#队伍数据[队伍id].成员数据 do
    local 临时id=队伍数据[队伍id].成员数据[n]
    五行年兽限制[临时id]=true
    任务数据[任务id].队伍组[#任务数据[任务id].队伍组+1]=临时id
    玩家数据[临时id].角色:添加任务(任务id)
  end
  常规提示(id,"#Y/你领取了五行年兽环式任务！")
end


