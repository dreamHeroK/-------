function 五行年兽:完成五行年兽任务(id,任务id,难度)
  任务数据[任务id].进程 = 任务数据[任务id].进程 + 1
  任务数据[任务id].积分 = 任务数据[任务id].积分 + 1
  任务数据[任务id].物品=nil
  任务数据[任务id].参数=nil
  任务数据[任务id].等级=nil
  if 任务数据[任务id].进程/3 == math.floor(任务数据[任务id].进程/3) then
    self:五行年兽杀怪(id,任务id)
  else
    self:五行年兽任务物品(id,任务id)
  end
end

