function 寻梦追忆:活动定时器()
	if 寻梦追忆活动时间.日期=="全天" then
		if 服务端参数.秒=="00" and 服务端参数.小时=="00" and 服务端参数.分钟=="00"then
			self.数据={}
		end
		if not self.活动开关 then
			self:开启活动()
		end
	else
		if 寻梦追忆活动时间.日期=="每天" then
			if 寻梦追忆活动时间.时==服务端参数.小时+0 and 寻梦追忆活动时间.分==服务端参数.分钟+0 and 寻梦追忆活动时间.秒<=服务端参数.秒+0 then
				self:开启活动()
			end
		else
			local zhouji=tonumber(os.date("%w", os.time()))
			if zhouji==寻梦追忆活动时间.日期 then
				if 寻梦追忆活动时间.时==服务端参数.小时+0 and 寻梦追忆活动时间.分==服务端参数.分钟+0 and 寻梦追忆活动时间.秒<=服务端参数.秒+0 then
					self:开启活动()
				end
			elseif 寻梦追忆活动时间.日期=="周1350" and (zhouji==1 or zhouji==3 or zhouji==5 or zhouji==0 ) then
				if 寻梦追忆活动时间.时==服务端参数.小时+0 and 寻梦追忆活动时间.分==服务端参数.分钟+0 and 寻梦追忆活动时间.秒<=服务端参数.秒+0  then
					self:开启活动()
				end
			elseif 寻梦追忆活动时间.日期=="周246" and (zhouji==2 or zhouji==4 or zhouji==6) then
				if 寻梦追忆活动时间.时==服务端参数.小时+0 and 寻梦追忆活动时间.分==服务端参数.分钟+0 and 寻梦追忆活动时间.秒<=服务端参数.秒+0  then
					self:开启活动()
				end
			end
		end
	end
end
