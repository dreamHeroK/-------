function 寻梦追忆:对话事件处理(id, 名称, 事件)
	if 事件 == "请吩咐" then
		if not self["活动开关"] then
			添加最后对话(id, "欢迎参加寻梦追忆活动，活动每周举行一次。你是否已经叫齐伙伴准备挑战了？当前不是活动时间，请关注群通知。")
			return
		elseif (玩家数据[id]["队伍"] == 0 or 取队伍人数(id) < 5 or 取队伍最低等级(玩家数据[id]["队伍"], 60)) then
			添加最后对话(id, "寻梦追忆参与条件：≥60级，≥5人")

			return
		elseif 玩家数据[id]["队长"] == false then
			添加最后对话(id, "这种重要的事情还是让队长来吧！")

			return

		end
  		if self:取寻梦追忆次数限制(id,2) then return end

		local go = true
		local 队伍id = 玩家数据[id]["队伍"]

		for n = 1, #队伍数据[队伍id]["成员数据"] do
			local cyid = 队伍数据[队伍id]["成员数据"][n]

			if self["数据"][cyid] and os["time"]() < self["数据"][cyid]["取消"] then
				添加最后对话(id, 玩家数据[cyid]["角色"]["名称"] .. "在五分钟内取消过任务，需等待五分钟后才可以继续任务哦。")

				if cyid ~= id then
					添加最后对话(cyid, "取消任务后，需等待5分钟后才可以继续领取。")
				end

				return
			end

			if 玩家数据[cyid]["角色"]:取任务(1167) ~= 0 then
				常规提示(cyid, "你已经有了一个这样的任务了！")

				if cyid ~= id then
					添加最后对话(id, 玩家数据[cyid]["角色"]["名称"] .. "已经领取过任务了")
				end

				go = false

				break
			end
		end

		if go then
			self:新增任务(id)
		end
	elseif 事件 == "我想取消任务" then
		local 任务id = 玩家数据[id]["角色"]:取任务(1167)

		if 任务id ~= 0 then
			玩家数据[id]["角色"]:取消任务(任务id)

			if self["活动开关"] then
				if self["数据"][id] == nil then
					self["数据"][id] = {
						["取消"] = 0,
						["环数"] = 1,
						["次数"] = 0
					}
				end

				self["数据"][id]["取消"] = os["time"]() + 300
			end

			添加最后对话(id, "已成功取消，并清空任务次数。5分钟可再次领取。")
		else
			添加最后对话(id, "你没有这样的任务哦。")
		end
	end
end

