	self:增加任务次数(id)

	for n = 1, #id组 do
		local cyid = id组[n]

		if self["活动开关"] and self["数据"][cyid] then
			self["数据"][cyid]["次数"] = self["数据"][id]["次数"]

			if self["数据"][cyid]["次数"] > 9 then
				local 链接 = {
					["频道"] = "hd",
					["结尾"] = "#W以资鼓励！",
					["提示"] = format("#P(寻梦追忆)#G/%s#W完成了又一轮寻梦追忆挑战，特赠送", 玩家数据[cyid]["角色"]["名称"])
				}

				玩家数据[cyid]["道具"]:给予超链接道具(cyid, "上古灵符", 1, nil, 链接)

				self["数据"][cyid]["环数"] = self["数据"][cyid]["环数"] + 1

				玩家数据[cyid]["角色"]:取消任务(玩家数据[cyid]["角色"]:取任务(1167))
				发送数据(玩家数据[cyid]["连接id"], 1501, {
