function 寻梦追忆:增加任务次数(id)
	if self["活动开关"] then
		if self["数据"][id] == nil then
			self["数据"][id] = {
				["取消"] = 0,
				["环数"] = 1,
				["次数"] = 0
			}
		end

		self["数据"][id]["次数"] = self["数据"][id]["次数"] + 1
	end
end

function 寻梦追忆:战斗胜利处理(id组, 战斗类型)
