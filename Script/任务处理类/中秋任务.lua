      玩家数据[id].地图单位=nil
    else
      添加最后对话(数字id,"你没有这样的任务")
    end
  end
end
function 中秋任务:设置中秋逐月任务(id)
  -- if not Q_中秋开关 then 添加最后对话(id,"中秋活动尚未开启,敬请期待~。") return end
  if 玩家数据[id].队伍 == 0 then 添加最后对话(id,"此活动可以组队完成,请最少5人组队参加") return  end
  if 取等级要求(id,50)==false then 添加最后对话(id,"参与此任务需要全队等级达到50级") return  end
  if self:取中秋逐月次数限制(id,2) then return end
  if 取队员任务存在(玩家数据[id].队伍,56) then 添加最后对话(id,"队伍中已有队员领取过此任务了") return  end
  local 任务id=id.."_56_"..os.time()
  local 结束时间=0
  任务数据[任务id]={
    id=任务id,
    起始=os.time(),
    结束=结束时间,
    玩家id=id,
    队伍组={},
    进程=1,
    类型=56
  }
  for i=1,#队伍数据[玩家数据[id].队伍].成员数据 do
    local lsid = 队伍数据[玩家数据[id].队伍].成员数据[i]
    if 中秋逐月数据[lsid] then
      中秋逐月数据[lsid] = 中秋逐月数据[lsid] + 1
    else
      中秋逐月数据[lsid] = 1
    end
    任务数据[任务id].队伍组[#任务数据[任务id].队伍组+1]=lsid
    玩家数据[lsid].角色:添加任务(任务id)
    发送数据(玩家数据[lsid].连接id,39)
    常规提示(lsid,"#Y/你参加了中秋逐月活动")
  end
  self:开启中秋任务()
end
function 中秋任务:战斗胜利处理(id组,任务id,战斗类型)
