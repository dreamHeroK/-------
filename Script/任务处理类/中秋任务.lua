function 中秋任务:取中秋逐月次数限制(id,次数)
local 队伍id=玩家数据[id].队伍
  for n=1,#队伍数据[队伍id].成员数据 do
    local 临时id=队伍数据[队伍id].成员数据[n]
    local cyid=队伍数据[队伍id].成员数据[n]
      if 活动次数数据.中秋任务.完成[临时id]== nil then
        活动次数数据.中秋任务.完成[临时id]=0
      end
      if 活动次数数据.中秋任务.完成[临时id]>=1 then
        常规提示(id,"#Y"..玩家数据[临时id].角色.名称.."本日中秋次数已达上限")
        return true
      else
        活动次数数据.中秋任务.完成[临时id]=活动次数数据.中秋任务.完成[临时id]+1
      end
  end
  return false
  end
function 中秋任务:添加中秋任务(id)
  if 玩家数据[id].角色.等级<20 then
    添加最后对话(id,"只有等级达到20级的玩家才可以领取本任务。")
    return
  elseif not Q_中秋开关 then 添加最后对话(id,"中秋活动尚未开启,敬请期待。")
    return
  elseif 玩家数据[id].角色:取任务(55) ~= 0 then
    添加最后对话(id,"你已经接取过该任务了,抓紧去完成吧。")
    return
  end
  local 任务id=id.."_55_"..os.time()
  local 结束时间=0
  任务数据[任务id]={
    id=任务id,
    起始=os.time(),
    结束=结束时间,
    玩家id=id,
    队伍组={},
    名称=1,
    模型=1,
    地图编号=1001,
    地图名称=1,
    积分=0,
    次数=1,
    类型=55
  }
  玩家数据[id].角色:添加任务(任务id)
  self:设置中秋任务(id,任务id)
end

function 中秋任务:设置中秋任务(id,任务id)
