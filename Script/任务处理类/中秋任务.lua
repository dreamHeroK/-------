function 中秋任务:完成中秋单人任务(id,任务id)
  if 任务数据[任务id] then
  --  local 奖励=奖励加载("中秋单人")
    local 等级=玩家数据[id].角色.等级
    local 额外加成=1+任务数据[任务id].次数*0.1
   -- local 名称组 = 奖励处理(id,奖励,等级,"中秋单人",额外加成)
     local 经验=qz(等级*8000*额外加成)
     local 银子=qz(等级*150*额外加成)
     玩家数据[id].角色:添加经验(经验,"中秋逐月")
     玩家数据[id].角色:添加银子(银子,"中秋逐月",1)
     if 玩家数据[id].角色.参战信息~=nil then
        玩家数据[id].召唤兽:获得经验(玩家数据[id].角色.参战宝宝.认证码,qz(经验*0.15),id,"中秋逐月")
      end
       local 获得物品={}
       for i=1,#自定义数据.中秋活动 do
           if 取随机数()<=自定义数据.中秋活动[i].概率 then
              获得物品[#获得物品+1]=自定义数据.中秋活动[i]
           end
       end
       获得物品=删除重复(获得物品)
       if 获得物品~=nil then
          local 取编号=取随机数(1,#获得物品)
          if 获得物品[取编号]~=nil and 获得物品[取编号].名称~=nil and 获得物品[取编号].数量~=nil then
             玩家数据[id].道具:自定义给予道具(id,获得物品[取编号].名称,获得物品[取编号].数量,获得物品[取编号].参数)
             广播消息({内容=format("#S/(中秋活动)#R/%s#Y/参加中秋逐月活动时表现卓越，获得#Y/婵娟#W/赠与的#G/%s#Y/",玩家数据[id].角色.名称,获得物品[取编号].名称),频道="xt"})
          end
       end
    local 积分 = 1
    if 任务数据[任务id].次数==6 or 任务数据[任务id].次数==7 then
      积分 = 1
    elseif 任务数据[任务id].次数==8 then
      积分 = 2
    elseif 任务数据[任务id].次数==9 or 任务数据[任务id].次数==10 then
      积分 = 3
    end
    玩家数据[id].角色.中秋积分 = 玩家数据[id].角色.中秋积分 + 积分
    if 任务数据[任务id].次数 >= 10 then
      任务数据[任务id].次数 = 0
    end
    任务数据[任务id].次数 = 任务数据[任务id].次数 + 1
    常规提示(id,"#Y/你获得了"..积分.."点中秋积分")
    self:设置中秋任务(id,任务id)
  end
end
