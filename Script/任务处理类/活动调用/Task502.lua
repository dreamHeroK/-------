	任务数据[任务id].地图名称=取地图名称(地图)
	任务数据[任务id].名称=名称
	任务数据[任务id].分类=类型
	任务数据[任务id].传说=nil
	if 类型==11 then
	 添加最后对话(id,format("前些日子%s的%s帮了我不少忙，请你前去替我感谢他吧。",任务数据[任务id].地图名称,任务数据[任务id].名称))
