function 完成任务_502(id,任务id)
	if 任务数据[任务id]==nil then return  end
	local 次数=任务数据[任务id].次数
	local 积分=0
	if 次数<=5 then
		积分=2
	elseif 次数<=20 then
		积分=3
	elseif 次数<=30 then
		积分=4
	elseif 次数<=40 then
		积分=5
	elseif 次数<=50 then
		积分=6
	elseif 次数<=60 then
		积分=7
	elseif 次数<=70 then
		积分=8
	elseif 次数<=80 then
		积分=9
	elseif 次数<=90 then
		积分=10
	else
		积分=11
	end
	任务数据[任务id].次数=任务数据[任务id].次数+1
	任务数据[任务id].积分=任务数据[任务id].积分+1
	-- if 玩家数据[id].队伍 ~= 0 then
	-- 	for n=1,#队伍数据[玩家数据[id].队伍].成员数据 do
	-- 		local id1 = 队伍数据[玩家数据[id].队伍].成员数据[n]
	-- 		if id1 ~= id then
	-- 			local 积分1 = qz(积分)
	-- 			玩家数据[id1].角色:添加玉魄积分(积分1)
	-- 		end
	-- 	end
	-- end
   	玩家数据[id].角色:添加玉魄积分(积分)
	if 任务数据[任务id].次数>=101 then
		local 链接 = {提示=format("#S(铸魂任务)#G/%s#W在#R铸魂任务#W任务中的完成了100环任务，获得",玩家数据[id].角色.名称),频道="hd",结尾="#W以资鼓励！#85"}
		local data = {"上古玉魄·阴","上古玉魄·阳"}
        		玩家数据[id].道具:给予超链接道具(id,data[取随机数(1,2)],1,nil,链接,"不存共享")
		玩家数据[id].角色:取消任务(任务id)
		常规提示(id,"#Y恭喜你完成了全部的铸魂任务")
		任务数据[任务id].次数=0
		任务数据[任务id].积分=0
		任务数据[任务id]=nil
	else
		GetUpMOB502(id,任务id)
	end
end

