function 设置任务138(id)
	print("刷新宝箱")
    local 地图范围 = {1110, 1070, 1091, 1193, 1092, 1208}
    local 选中地图 = {}
    local x待发送数据 = {}
    local 模型数量表 = {
        {"黄铜宝箱", 10},  -- 黄铜宝箱：每个地图刷10个
        {"乌金宝箱", 5},   -- 乌金宝箱：每个地图刷5个
        {"赤金宝箱", 3},   -- 赤金宝箱：每个地图刷3个（可根据需求修改）
        {"神奇宝箱", 2}    -- 神奇宝箱：每个地图刷2个（可根据需求修改）
    }
    while #选中地图 < 3 do
        local 随机索引 = 取随机数(1, #地图范围)
        local 候选地图 = 地图范围[随机索引]
        local 已存在 = false
        for _, v in ipairs(选中地图) do
            if v == 候选地图 then
                已存在 = true
                break
            end
        end
        if not 已存在 then
            table.insert(选中地图, 候选地图)
        end
    end

    for _, 地图 in ipairs(选中地图) do
        x待发送数据[地图] = {} -- 按地图存储待发送数据
        for _, 模型数据 in ipairs(模型数量表) do
            local 当前模型 = 模型数据[1] -- 当前宝箱类型（如黄铜宝箱）
            local 当前数量 = 模型数据[2] -- 当前模型的刷新数量（如10）
            for n = 1, 当前数量 do
                local 任务id = 地图 .. "_138_" .. os.time() .. "_" .. 随机序列 .. "_" .. 取随机数(88, 99999999) .. "_" .. n
                随机序列 = 随机序列 + 1
                local xy = 地图处理类.地图坐标[地图]:取随机点()
                任务数据[任务id] = {
                    id = 任务id,
                    起始 = os.time(),
                    结束 = 1500, -- 存在时间25分钟（1500秒）
                    玩家id = 0,
                    队伍组 = {},
                    名称 = 当前模型,
                    模型 = "赤金宝箱",
                    等级 = 取随机数(1, 140),
                    称谓 = nil,
                    x = xy.x,
                    y = xy.y,
                    事件 = "明雷活动",
                    地图编号 = 地图,
                    地图名称 = 取地图名称(地图),
                    类型 = 138
                }
                table.insert(x待发送数据[地图], 地图处理类:批量添加单位(任务id))
            end
        end
    end
    for _, 地图编号 in ipairs(选中地图) do
        for 玩家id, _ in pairs(地图处理类.地图玩家[地图编号]) do
            if 玩家id ~= id and 地图处理类:取同一地图(地图编号, id, 玩家id, 1) then
                发送数据(玩家数据[玩家id].连接id, 1021, x待发送数据[地图编号])
            end
        end
    end
    local 地图名称列表 = {}
    for _, 地图编号 in ipairs(选中地图) do
        table.insert(地图名称列表, 取地图名称(地图编号))
    end
    广播消息({
        内容 = format("#Y一阵风吹过，#G%s、%s、%s#Y刷新了大量宝箱，25分钟内可以去拾取，快去探索吧！",
            地图名称列表[1], 地图名称列表[2], 地图名称列表[3]),
        频道 = "xt" -- 系统频道广播
    })

    x待发送数据 = {}
end


