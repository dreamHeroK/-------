function 简单_双龙之战:NPC捐赠(id, 类型, 序列)

    if not 玩家数据[id] or not 玩家数据[id].角色 then
        常规提示(id, "#Y玩家数据异常，请稍后再试。#24")
        return
    end
    if not self.数据 or not self.数据.好感NPC then
        常规提示(id, "#Y活动数据异常，请稍后再试。#24")
        return
    end
    if self.数据.活动积分 < 捐赠配置.积分消耗 then
        常规提示(id, string.format("#Y当前活动积分不足#R%d#Y点。#24", 捐赠配置.积分消耗))
        return
    end
    if 玩家数据[id].角色.当前经验 < 捐赠配置.经验消耗 then
        常规提示(id, string.format("#Y当前经验不足#R%d#Y点。#24", 捐赠配置.经验消耗))
        return
    end
    if not self.数据.好感NPC[序列] then
        self.数据.好感NPC[序列] = {}
        for i = 1, 捐赠配置.每页NPC数量 do
            self.数据.好感NPC[序列][i] = 0
        end
    end
    local 当前好感度 = self.数据.好感NPC[序列][类型] or 0
    local 好感度上限 = 序列 * 100
    if 当前好感度 >= 好感度上限 then
        常规提示(id, "#Y当前NPC好感度已满，无需捐赠。#24")
        return
    end
    if 序列 > 1 then
        local 上一页 = 序列 - 1
        if not self.数据.好感NPC[上一页] then
            常规提示(id, string.format("#Y请先完成第#R%d#Y页所有NPC的捐赠，再进行当前页操作。#45", 上一页))
            return
        end
        local 上一页是否全部完成 = true
        for i = 1, 捐赠配置.每页NPC数量 do
            if self.数据.好感NPC[上一页][i] <上一页*100 then
                上一页是否全部完成 = false
                break -- 找到一个未达标，直接退出循环
            end
        end
        if not 上一页是否全部完成 then
            常规提示(id, string.format("#Y请先将第#R%d#Y页所有NPC好感度提升至#R"..(上一页*100).."#Y，再进行当前页捐赠。#45", 上一页))
            return
        end
    end
    local 配置文件路径 = 程序目录 .. [[data\]] .. 玩家数据[id].账号 .. [[\账号信息.txt]]
    if f函数.读配置(配置文件路径, "账号配置", "NPC捐赠") == "空" then
        f函数.写配置(配置文件路径, "账号配置", "NPC捐赠", 0)
    end
    玩家数据[id].阵营NPC捐赠 = f函数.读配置(配置文件路径, "账号配置", "NPC捐赠") + 0

    if 玩家数据[id].阵营NPC捐赠 > os.time() then
        local 剩余时间 = 玩家数据[id].阵营NPC捐赠 - os.time()
        常规提示(id, string.format("#Y捐赠过于频繁，请等待#R%d#Y秒后再试。#24", 剩余时间))
        return
    end
    self.数据.活动积分 = self.数据.活动积分 - 捐赠配置.积分消耗
    玩家数据[id].角色:扣除经验(捐赠配置.经验消耗)
