-- 新增：一键修炼操作
function 简单_称号系统:一键修炼操作(id,选中,助战id)
	local 主人id = id+0
	if 助战id then
	id = 助战id+0
	end
	选中=选中+0

	-- 安全获取称号数据
	local 称号数据 = 玩家数据[id].角色.称号系统
	local 当前等级 = 称号数据.当前等级 or 1
	local 当前突破 = 称号数据.当前突破 or 0
	local 当前经验 = 称号数据.当前经验 or 0
	local 突破经验 = 称号数据.突破经验 or 20

	-- 检查是否满足修炼条件
	if 当前等级 > 10 then
       		常规提示(主人id,"#Y/当前无法进行提升")
		return
	end
	if (选中 or 0) > 当前等级 then
       		常规提示(主人id,"#P/当前称号系统等级为："..当前等级.."，无法进行提升")
		return
	end
	if 当前突破 >= 5 and 当前经验 >= 突破经验 then
       		常规提示(主人id,"#P/当前已修炼满级，请进行等级突破")
		return
	end

	-- 检查地宫层数限制
	if 雁塔地宫.数据[id] == nil then
		雁塔地宫.数据[id] = {层数=1,次数=0}
	end
	雁塔地宫.数据[id].层数 = 雁塔地宫.数据[id].层数 or 1
	if 当前等级 * 10 > 雁塔地宫.数据[id].层数 and 调试模式 == false then
		常规提示(主人id,"#Y/当前地宫层数：#R/"..雁塔地宫.数据[id].层数.."层#Y/，继续修炼需要到达：#R/"..(当前等级 * 10).."层")
		return
	end

	-- 计算需要多少经验才能填满当前进度条
	local 需要经验 = 突破经验 - 当前经验
	-- 每个晶体+10经验，计算需要多少个晶体
	local 需要晶体数 = math.ceil(需要经验 / 10)
	if 需要晶体数 < 1 then 需要晶体数 = 1 end

	-- 检查背包中晶体数量
	local 拥有晶体数 = 玩家数据[主人id].道具:判断道具数量(主人id, "蕴灵晶体")
	if 拥有晶体数 < 1 then
       		常规提示(主人id,"#P/当前没有足够的(蕴灵晶体)进行修炼")
		return
	end

	-- 实际能使用的晶体数量（取需要数和拥有数的最小值）
	local 实际使用晶体数 = math.min(需要晶体数, 拥有晶体数)
	local 实际增加经验 = 实际使用晶体数 * 10

	-- 消耗晶体
	玩家数据[主人id].道具:消耗背包道具(玩家数据[主人id].连接id,主人id,"蕴灵晶体",实际使用晶体数)

	-- 增加经验
	称号数据.当前经验 = 当前经验 + 实际增加经验

	-- 重新获取更新后的经验值
	local 新经验 = 称号数据.当前经验

	-- 检查是否升级突破
	if 新经验 >= 突破经验 then
		-- 如果当前突破未满5级，则升级突破
		if 当前突破 < 5 then
			称号数据.当前突破 = 当前突破 + 1
			称号数据.突破经验 = 20 * 当前等级 * (当前突破 + 1)
			称号数据.当前经验 = 0
			发送数据(玩家数据[主人id].连接id,440001,称号数据.当前突破)
			常规提示(主人id,"#G/一键修炼成功！消耗"..实际使用晶体数.."个蕴灵晶体，当前突破等级提升至"..称号数据.当前突破.."级")
		else
			-- 已满5级突破，经验填满即可突破
			常规提示(主人id,"#G/一键修炼成功！消耗"..实际使用晶体数.."个蕴灵晶体，修炼进度已满，可以进行突破了")
		end
	else
		常规提示(主人id,"#G/一键修炼成功！消耗"..实际使用晶体数.."个蕴灵晶体")
	end

	self:称号显示信息(主人id,助战id)
end

function 简单_称号系统:称号突破操作(id,选中,助战id)
	local 主人id = id+0
	if 助战id then
	id = 助战id+0
	end
	选中=选中+0

	-- 安全获取称号数据
	local 称号数据 = 玩家数据[id].角色.称号系统
	local 当前等级 = 称号数据.当前等级 or 1
	local 当前突破 = 称号数据.当前突破 or 0

	if (选中 or 0) > 当前等级 then
       		常规提示(主人id,"#P/当前称号系统等级为："..当前等级.."，无法进行提升")
		return
	end

	-- 检查是否至少添加了一种材料（提前检查，提示更直观）
	local 已添加材料 = 称号数据.增加1 or 称号数据.增加2 or 称号数据.增加3 or 称号数据.增加4
	if not 已添加材料 then
		常规提示(主人id,"#R/请先添加至少一种突破材料后再进行突破！")
		return
	end

	if 当前突破 < 5 then
       		常规提示(主人id,"#P/当前突破等级未满5级，无法进行突破")
		return
	end

	local cgl = 100 - (当前等级 * 14)
	if cgl < 0 then cgl = 0 end
	if 称号数据.增加1 then  cgl = cgl + 5 end
	if 称号数据.增加2 then  cgl = cgl + 8 end
	if 称号数据.增加3 then  cgl = cgl + 12 end
	if 称号数据.增加4 then  cgl = cgl + 20 end
	if cgl > 100 then cgl = 100 end
	if 取随机数(1,100) < cgl then
		称号数据.当前等级 = 当前等级 + 1
		称号数据.当前突破 = 0
		称号数据.增加1 = false
		称号数据.增加2 = false
		称号数据.增加3 = false
		称号数据.增加4 = false
		称号数据.当前经验 = 0
       		玩家数据[id].角色:刷新信息()  -- 刷新属性
		发送数据(玩家数据[id].连接id, 31, 玩家数据[id].角色:取总数据())  -- 发送完整属性数据
      		常规提示(主人id,"#G/恭喜你本次突破成功")
	else
		称号数据.当前突破 = 当前突破 - 1
		称号数据.增加1 = false
		称号数据.增加2 = false
		称号数据.增加3 = false
		称号数据.增加4 = false
		称号数据.当前经验 = 0
       		玩家数据[id].角色:刷新信息()  -- 刷新属性
		发送数据(玩家数据[id].连接id, 31, 玩家数据[id].角色:取总数据())  -- 发送完整属性数据
      		常规提示(主人id,"#R/很遗憾，本次突破失败，突破等级掉落一级")
	end
	self:称号显示信息(主人id,助战id)

end
