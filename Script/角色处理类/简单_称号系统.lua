	elseif  选中==4 and 称号数据.增加4 == true then
		return 常规提示(主人id,"#Y/当前道具仅能使用一个，已经增加过几率了哦")
	end
	玩家数据[主人id].道具:消耗背包道具(玩家数据[主人id].连接id,主人id,道具名称,1)
	if 选中==1 then
		称号数据.增加1=true
		常规提示(主人id,"#Y/道具添加成功，当前已为突破几率增加5%")
	elseif  选中==2 then
		称号数据.增加2=true
		常规提示(主人id,"#Y/道具添加成功，当前已为突破几率增加8%")
	elseif  选中==3 then
		称号数据.增加3=true
		常规提示(主人id,"#Y/道具添加成功，当前已为突破几率增加12%")
	elseif  选中==4 then
		称号数据.增加4=true
		常规提示(主人id,"#Y/道具添加成功，当前已为突破几率增加20%")
