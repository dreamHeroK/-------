	if 助战id then
	id = 助战id+0
	end
	选中=选中+0

	-- 安全获取称号数据
	local 称号数据 = 玩家数据[id].角色.称号系统
	local 当前突破 = 称号数据.当前突破 or 0
	local 当前经验 = 称号数据.当前经验 or 0
	local 突破经验 = 称号数据.突破经验 or 20

	if 当前突破 < 5 then
       		常规提示(主人id,"#P/请修炼至满级后再进行道具添加")
		return
	end
	if 当前经验 < 突破经验 then
       		常规提示(主人id,"#P/请修炼至满级后再进行道具添加")
		return
	end
	local  道具名称=""
	if 选中==1 then 道具名称="破境·白虎坠"
	elseif  选中==2 then 道具名称="破境·朱雀坠"
	elseif  选中==3 then 道具名称="破境·玄武坠"
	elseif  选中==4 then 道具名称="破境·青龙坠"  end
	if 道具名称=="" then
		return
	end

	if 玩家数据[主人id].道具:判断道具数量(主人id, 道具名称) <1 then
       		常规提示(主人id,"#P/当前没有足够的道具进行增加成功率")
		return
	end
	-- 安全检查是否已添加
	if 选中==1 and 称号数据.增加1 == true then
		return 常规提示(主人id,"#Y/当前道具仅能使用一个，已经增加过几率了哦")
	elseif  选中==2 and 称号数据.增加2 == true then
		return 常规提示(主人id,"#Y/当前道具仅能使用一个，已经增加过几率了哦")
