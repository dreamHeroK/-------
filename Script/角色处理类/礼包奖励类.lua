function 礼包奖励类:增加宠物(id,名称,类型,等级,额外技能数量,变异概率)
	local bbb = {"高级夜战","高级偷袭","高级吸血"}
	local 技能组 = {}
	if 类型=="攻" then
		for i=1,额外技能数量 do
			local xh = 取随机数(1,#bbb)
			技能组[#技能组+1] =  bbb[xh]
			table.remove(bbb,xh)
		end
		技能组[#技能组+1] =  "高级必杀"
		技能组[#技能组+1] =  "高级连击"
	else
		bbb = {"魔之心","敏捷","神佑复生","感知","法术暴击","法术波动"}
		local daf = {"地狱烈火","奔雷咒","泰山压顶","水漫金山"}
		for i=1,额外技能数量 do
			local xh = 取随机数(1,#bbb)
			技能组[#技能组+1] =  bbb[xh]
			table.remove(bbb,xh)
		end
		技能组[#技能组+1] =  "高级法术连击"
		技能组[#技能组+1] =  daf[取随机数(1,#daf)]
	end
	local 类型="宝宝"
	if 变异概率 then
		if 取随机数()<=变异概率 then
			类型="变异"
		end
	end
	玩家数据[id].召唤兽:添加召唤兽(名称, 名称, 类型, nil, 等级 , nil , 技能组)
	常规提示(id,"#Y/你获得了一只"..名称)
end
