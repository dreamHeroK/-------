function 礼包奖励类:签到奖励处理(id, 数据)
	local 月份 =tonumber(os.date("%m", os.time()))
	--local 数据类型 = "修改签到数据"
	-- if 全局签到数据[id] == nil then
	--   	数据类型 = "写入签到数据"
	-- end
	if 全局签到数据[id] == nil or 全局签到数据[id][月份] == nil then
		系统处理类:获取签到数据(id)
		return
	end
	if 全局签到数据[id][月份][数据.奖励类型] then
		常规提示(id,"#Y/你已经领取过该奖励了")
		系统处理类:获取签到数据(id)
		return
	end
	local 奖励
	for i=1,#全局礼包数据.签到奖励 do
	    if 全局礼包数据.签到奖励[i].礼包类型==数据.奖励类型 then
	    	奖励 =  i
	    	break
	    end
	end
	if 奖励 then
		if 全局签到数据[id][月份].累计签到 < 全局礼包数据.签到奖励[奖励].领取条件 then
			常规提示(id,"#Y/请先完成签到！")
		else
			local 领取物品 = {}
			for n = 1, 10 do
 				if 全局礼包数据.签到奖励[奖励]["物品"..n] ~= nil then
 					领取物品[#领取物品 + 1] = table.copy(全局礼包数据.签到奖励[奖励]["物品"..n])
 				end
 			end
 			全局签到数据[id][月份][数据.奖励类型]=true
 			self:领取定义礼包(id, 领取物品)
 			local 队伍id = 玩家数据[id].队伍
 			if 队伍id and 队伍id~=0 and 队伍数据[队伍id] then
 				for n=1,#队伍数据[队伍id].成员数据 do
 					local cyid = 队伍数据[队伍id].成员数据[n]
 					if cyid ~= id and 玩家数据[cyid] ~= nil then
 						if 全局签到数据[cyid] == nil or 全局签到数据[cyid][月份] == nil then
 							系统处理类:获取签到数据(cyid)
 						end
 						if 全局签到数据[cyid] and 全局签到数据[cyid][月份] and not 全局签到数据[cyid][月份][数据.奖励类型]
 							and 全局签到数据[cyid][月份].累计签到 >= 全局礼包数据.签到奖励[奖励].领取条件 then
 							全局签到数据[cyid][月份][数据.奖励类型]=true
 							self:领取定义礼包(cyid, table.copy(领取物品))
 							常规提示(cyid,"#Y/队友领取了签到奖励，你也获得了一份！")
 							发送数据(玩家数据[cyid].连接id,116.2,全局签到数据[cyid][月份])
 						end
 					end
 				end
 			end
		end
	else
		常规提示(id,"#Y/奖励不存在！")
	end
	发送数据(玩家数据[id].连接id,116.2,全局签到数据[id][月份])
end

