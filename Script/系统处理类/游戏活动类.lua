			玩家数据[id].道具:给予道具(id,名称,1)
			广播消息({内容=format("#S(房都尉答题)#R/%s#Y在房都尉答题表现突出，仅用#W/%s#Y秒的时间就正确作答#W/%s#Y/道题目，因此获得了房都尉特别奖励的#G/%s",玩家数据[id].角色.名称,房都尉答题数据[id].耗时,房都尉答题数据[id].答对,名称),频道="xt"})
		end
