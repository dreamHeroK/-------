		local 经验=等级*1000
		local 银子=等级*1000+5000
		玩家数据[id].角色:添加经验(经验,"房都尉答题",1)
		玩家数据[id].角色:添加银子(储备,"房都尉答题",1)
		local 奖励参数=取随机数(1,200)
		if 奖励参数<=20 then
