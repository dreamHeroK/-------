	玩家数据[id].房都尉对话=true
	房都尉答题数据[id].计时=os.time()
	房都尉答题数据[id].总数=房都尉答题数据[id].总数+1
	发送数据(连接id,1501,{名称="房都尉",模型="男人_马副将",对话=format("#Y/第%s题：#W/%s",房都尉答题数据[id].总数, 玩家数据[id].房都尉答题数据.题目),选项= 玩家数据[id].房都尉答题数据.答案})
end

