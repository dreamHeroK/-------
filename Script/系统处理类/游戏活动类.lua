        光环名称 = 玩家数据[id].道具.数据[玩家数据[id].角色.锦衣[3]].名称
    end

    科举数据[id].记录 = {
        答对 = 0,
        id = id,
        名称 = 玩家数据[id].角色.名称,
        模型 = 玩家数据[id].角色.模型,
        武器 = 武器名称,
        副武器=副武器名称,
        锦衣 = 锦衣名称,
        光环=光环名称
    }

    设置任务7(id)
    self:设置科举题目(连接id,id)
end

function 游戏活动类:设置科举题目(连接id,id)
    local 序列 = 取随机数(1, #科举题库)
    local 正确答案 = 科举题库[序列][4]

    local 随机答案 = {}
    for n = 2, 4 do
        随机答案[n-1] = {答案 = 科举题库[序列][n], 序列 = 取随机数(1, 9999)}
    end

    table.sort(随机答案, function(a,b) return a.序列 > b.序列 end)

    local 显示答案 = {}
    for n = 1, 3 do
        显示答案[n] = 随机答案[n].答案
    end
    显示答案[#显示答案+1] = "使用法宝"

    玩家数据[id].科举数据 = {
        题目 = 科举题库[序列][1],
        答案 = 显示答案,
        正确答案 = 正确答案
    }

    玩家数据[id].科举对话 = true
    科举数据[id].计时 = os.time()
    科举数据[id].总数 = 科举数据[id].总数 + 1

    发送数据(连接id, 1501, {
        名称 = "礼部侍郎",
        模型 = "考官2",
        对话 = format("#Y/第%s题：#W/%s", 科举数据[id].总数, 玩家数据[id].科举数据.题目),
        选项 = 玩家数据[id].科举数据.答案
    })
end

