		常规提示(id,"#Y/你当前累积错误题目数量已达10道，已经无法参加后续的答题了")
		房都尉答题数据[id].完成=true
		写出文件([[sql/房都尉答题数据.txt]],table.tostring(房都尉答题数据))
	else
		self:设置房都尉答题题目(连接id,id)
	end
end

