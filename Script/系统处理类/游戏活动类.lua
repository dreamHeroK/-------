        添加最后对话(id, "#Y/你本日可参加科举活动的次数已经耗尽，请明日再来吧")
        return
    elseif 取等级(id) < 60 then
        添加最后对话(id, "#Y/只有等级达到60级的玩家才可参加科举活动")
        return
    end

    if 科举数据[id] == nil then
        科举数据[id] = {次数 = 0}
    end

    科举数据[id].次数 = 科举数据[id].次数 + 1
    科举数据[id].天王令 = 1
    科举数据[id].地王令 = 2
    科举数据[id].鬼王令 = 3
    科举数据[id].答对 = 0
    科举数据[id].答错 = 0
    科举数据[id].总数 = 0
    科举数据[id].连对 = 0
    科举数据[id].起始 = os.time()

    local 武器名称 = nil
    local 锦衣名称 = nil
    local 光环名称 = nil
    local 副武器名称=nil
    if 玩家数据[id].角色.装备[3] and 玩家数据[id].道具.数据[玩家数据[id].角色.装备[3]] then
        武器名称 = 玩家数据[id].道具.数据[玩家数据[id].角色.装备[3]].名称
    end
    if 玩家数据[id].角色.模型 == "影精灵" then
        local 装备ID = 玩家数据[id].角色.装备[4]
        if 装备ID and 玩家数据[id].道具.数据[装备ID] then
            副武器名称 = 玩家数据[id].道具.数据[装备ID].名称
        end
    end

    if 玩家数据[id].角色.锦衣[1] and 玩家数据[id].道具.数据[玩家数据[id].角色.锦衣[1]] then
        锦衣名称 = 玩家数据[id].道具.数据[玩家数据[id].角色.锦衣[1]].名称
    end
