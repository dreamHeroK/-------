function 游戏活动类:添加到科举排行榜(记录)
    if not 记录 or not 记录.答对 then
        return
    end

    -- 先检查该玩家是否已经在排行榜中
    local 玩家索引 = nil
    for i, 现有记录 in ipairs(科举排行) do
        if 现有记录 and 现有记录.id == 记录.id then
            玩家索引 = i
            break
        end
    end

    -- 如果玩家已经在排行榜中
    if 玩家索引 then
        -- 比较新旧记录，保留更好的
        local 旧记录 = 科举排行[玩家索引]
        if 记录.答对 > 旧记录.答对 or (记录.答对 == 旧记录.答对 and 记录.耗时 < 旧记录.耗时) then
            -- 新记录更好，替换旧记录
            科举排行[玩家索引] = 记录
        else
            -- 旧记录更好，不做任何改变
            return
        end
    else
        -- 玩家不在排行榜中
        -- 如果排行榜未满3人，直接添加
        if #科举排行 < 3 then
            table.insert(科举排行, 记录)
        else
            -- 检查是否比最后一名好
            local 最后一名 = 科举排行[3]
            if 记录.答对 > 最后一名.答对 or (记录.答对 == 最后一名.答对 and 记录.耗时 < 最后一名.耗时) then
                科举排行[3] = 记录
            else
                -- 不如最后一名，不加入排行榜
                return
            end
        end
    end

    -- 排序
    table.sort(科举排行, function(a, b)
        if a.答对 ~= b.答对 then
            return a.答对 > b.答对
        else
            return a.耗时 < b.耗时
        end
    end)

    -- 更新游戏内单位显示
    -- self:科举单位变更()
end



