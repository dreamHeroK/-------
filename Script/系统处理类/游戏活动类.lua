				正确=true
			end
		else
			常规提示(id,"#Y/答题超时！")
		end
	elseif 类型==4 then
		正确=true
	end
	if 正确 then
		房都尉答题数据[id].答对=房都尉答题数据[id].答对+1
		房都尉答题数据[id].连对= 房都尉答题数据[id].连对+1
