			local 名称=取宝石()
			玩家数据[id].道具:给予道具(id,名称,1)
			广播消息({内容=format("#S(房都尉答题)#R/%s#Y在房都尉答题中如有神助，妙笔生花，获得了房都尉奖励的#G/%s",玩家数据[id].角色.名称,名称),频道="xt"})
		elseif 奖励参数<=50 then
			local 名称="金柳露"
			玩家数据[id].道具:给予道具(id,名称,1)
			广播消息({内容=format("#S(房都尉答题)#R/%s#Y在房都尉答题中如有神助，妙笔生花，获得了房都尉奖励的#G/%s",玩家数据[id].角色.名称,名称),频道="xt"})
		end
	else
		房都尉答题数据[id].答错=房都尉答题数据[id].答错+1
		房都尉答题数据[id].连对=0
		常规提示(id,"#Y/你的回答不正确")
	end
	if 房都尉答题数据[id].总数>=20 then
		房都尉答题数据[id].耗时=os.time()- 房都尉答题数据[id].起始
		if 房都尉答题数据[id].答对>=15 then
			local 等级=取等级(id)
			local 经验=等级*800*房都尉答题数据[id].答对
			local 储备=等级*100*房都尉答题数据[id].答对
			玩家数据[id].角色:添加经验(经验,"房都尉答题活动最终奖励",1)
