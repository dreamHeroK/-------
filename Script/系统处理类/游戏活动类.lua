function 游戏活动类:刷新科举排行假人()
    local 删除列表 = {}
    for 任务id, 任务信息 in pairs(任务数据) do
        if 任务信息 ~= nil and 任务信息.类型 == 3589 and
           任务信息.标识 ~= nil and 任务信息.标识 == "科举排行单位" then
            table.insert(删除列表, 任务id)
        end
    end
    for _, 任务id in ipairs(删除列表) do
        if 任务数据[任务id] and 任务数据[任务id].地图编号 and 任务数据[任务id].单位编号 then
            地图处理类:删除单位(任务数据[任务id].地图编号, 任务数据[任务id].单位编号)
        end
        任务数据[任务id] = nil
    end

    -- 先清空内存中的科举排行数据
    科举排行 = {}

    -- 清空文件
    写出文件([[sql/科举排行榜.txt]], table.tostring(科举排行))

    -- 重新初始化单位
    self:科举单位初始化()

    -- 重新加载排行榜数据
    self:加载科举排行榜()

    return true
end

