function 糖果专用系统:召唤兽切换锦衣(id)
    if 玩家数据[id].角色.参战信息==nil then
        添加最后对话(id,"请将需要切换的宝宝参战！")
        return
    end
    local 编号=玩家数据[id].召唤兽:取编号(玩家数据[id].角色.参战宝宝.认证码)
    if 编号~=0 and 玩家数据[id].召唤兽.数据[编号] then
        if 玩家数据[id].召唤兽.数据[编号].饰品~=nil then
            添加最后对话(id,"请先卸下召唤兽饰品！")
            return
        elseif 玩家数据[id].召唤兽.数据[编号].锦衣编号==nil then
            添加最后对话(id,"#17少侠，该召唤兽未获得锦衣或者召唤兽造型未开放，无法切换！！")
            return
        elseif 玩家数据[id].召唤兽.数据[编号].锦衣编号==0 then
            添加最后对话(id,"#17少侠，该召唤兽未获得锦衣或者召唤兽造型未开放，无法切换！！")
            return
        end
        if string.find(玩家数据[id].召唤兽.数据[编号].模型,"锦衣")~=nil then
            if 玩家数据[id].角色:扣除银子(5000000,0,0,"切换宝宝造型") then
              玩家数据[id].召唤兽.数据[编号].模型=string.gsub(玩家数据[id].召唤兽.数据[编号].模型,"锦衣","")
              -- if 调试模式 then
              --     玩家数据[id].道具:给予道具(id,"召唤兽锦衣",1)
              --     玩家数据[id].召唤兽.数据[编号].锦衣编号=0
              --     print("召唤兽锦衣")
              -- end
                if tonumber(玩家数据[id].召唤兽.数据[编号].攻击资质) ~= nil then
                  玩家数据[id].召唤兽.数据[编号].攻击资质 = 玩家数据[id].召唤兽.数据[编号].攻击资质
                end
                if tonumber(玩家数据[id].召唤兽.数据[编号].防御资质) ~= nil then
                  玩家数据[id].召唤兽.数据[编号].防御资质 = 玩家数据[id].召唤兽.数据[编号].防御资质
                end
                if tonumber(玩家数据[id].召唤兽.数据[编号].体力资质) ~= nil then
                  玩家数据[id].召唤兽.数据[编号].体力资质 = 玩家数据[id].召唤兽.数据[编号].体力资质
                end
                if tonumber(玩家数据[id].召唤兽.数据[编号].法力资质) ~= nil then
                  玩家数据[id].召唤兽.数据[编号].法力资质 = 玩家数据[id].召唤兽.数据[编号].法力资质
                end
                if tonumber(玩家数据[id].召唤兽.数据[编号].速度资质) ~= nil then
                  玩家数据[id].召唤兽.数据[编号].速度资质 = 玩家数据[id].召唤兽.数据[编号].速度资质
                end
                if tonumber(玩家数据[id].召唤兽.数据[编号].躲闪资质) ~= nil then
                  玩家数据[id].召唤兽.数据[编号].躲闪资质 = 玩家数据[id].召唤兽.数据[编号].躲闪资质
