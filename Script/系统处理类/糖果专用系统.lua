                    end
                    if tonumber(玩家数据[id].召唤兽.数据[编号].速度资质) ~= nil then
                      玩家数据[id].召唤兽.数据[编号].速度资质 = 玩家数据[id].召唤兽.数据[编号].速度资质
                    end
                    if tonumber(玩家数据[id].召唤兽.数据[编号].躲闪资质) ~= nil then
                      玩家数据[id].召唤兽.数据[编号].躲闪资质 = 玩家数据[id].召唤兽.数据[编号].躲闪资质
