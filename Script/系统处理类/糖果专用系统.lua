                end
                发送数据(id,6556,玩家数据[id].召唤兽.数据[编号])
                发送数据(玩家数据[id].连接id,6556,玩家数据[id].召唤兽.数据[编号])
                添加最后对话(id,"恭喜你的召唤兽获得了新的形态！")
            end
        else
            local mx="锦衣"..玩家数据[id].召唤兽.数据[编号].模型
            local go=false
            if self:取召唤兽锦衣(mx) then
                go=true
            end
            if go then
                if 玩家数据[id].角色:扣除银子(5000000,0,0,"切换宝宝造型") then
                    玩家数据[id].召唤兽.数据[编号].模型=mx
