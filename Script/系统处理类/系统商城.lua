function 系统商城:数据处理(id,序号,内容)
    if 序号 == 901  then
        self:刷新交易数据(id,内容)
    elseif 序号 == 902 then
        self:购买交易中心商品(id,内容)
    elseif 序号 == 903 then
        self:出售交易中心商品(id,内容)
    elseif 序号 == 904 then
        self:刷新交易中心数量(id)
    elseif 序号 == 905 then -- 新增：重新加载配置
        self:重新加载配置(id)
    end
end

