function 系统商城:购买交易中心商品(id,内容)
    local 大类=内容.大类 + 0
    local 小类=内容.小类
    local 编号=内容.编号 + 0
    local 商品列表,小类序号=获取交易中心列表(大类,小类)
    local 选中商品 = 商品列表[编号]

    -- 检查商品数量
    if 选中商品.数量 <= 0 then
        发送数据(玩家数据[id].连接id, 7, "#y/该商品已售罄，无法购买")
        return
    end

    if 选中商品.日涨跌幅 >= 10.0 then
        发送数据(玩家数据[id].连接id, 7, "#y/该商品已经涨停了，无法购买")
        return
    end

    if 玩家数据[id].角色.银子<选中商品.价格 then
        常规提示(id,"#Y/你没有那么多的银子")
        return
    end

    local 临时格子=玩家数据[id].角色:取道具格子()
    if 临时格子==0 then
        常规提示(id,"#Y/包裹已满，请先整理下包裹吧！")
        return
    end
    玩家数据[id].角色:扣除银子(选中商品.价格,0,0,"交易中心购买",1)

    -- 处理杂货类物品
    if 大类 == 3 then
        if 选中商品.名称 == "特效宝珠" then
            玩家数据[id].道具:给予道具(id,选中商品.名称,选中商品.参数一+0,nil,"商店")
        elseif 选中商品.名称 == "特性宝珠" then
            玩家数据[id].道具:给予道具(id,选中商品.名称,选中商品.参数一+0,nil,"商店")
        elseif 选中商品.名称 == "武器专属石" then
            玩家数据[id].道具:给予道具(id,选中商品.名称,选中商品.参数一+0,nil,"商店")
        end
    else
        if 选中商品.名称 == "魔兽要诀" or 选中商品.名称 == "高级魔兽要诀" then
            玩家数据[id].道具:给予道具(id,选中商品.名称,nil,选中商品.参数一,"商店")
        elseif 选中商品.名称 == "制造指南书" then
            玩家数据[id].道具:给予道具(id,选中商品.名称,选中商品.参数一+0,选中商品.参数二+0,"商店")
        elseif 选中商品.名称 == "百炼精铁" then
            玩家数据[id].道具:给予道具(id,选中商品.名称,选中商品.参数一+0,nil,"商店")
        end
    end

    玩家数据[id].角色:日志记录(format("[交易中心-购买]购买道具[%s][%s]，花费%s两银子：[%s][%s][%s]",选中商品.名称,选中商品.参数一,选中商品.价格,玩家数据[id].账号,id,玩家数据[id].角色.名称))
    常规提示(id,"#Y/你花费了#R"..选中商品.价格.."#Y两，成功购买了#R"..选中商品.名称.."！")

    -- 更新价格和涨跌幅
    交易中心[大类][小类序号].内容[编号].价格 = math.floor(交易中心[大类][小类序号].内容[编号].价格 / (100+交易中心[大类][小类序号].内容[编号].日涨跌幅) * (100+交易中心[大类][小类序号].内容[编号].日涨跌幅 + 0.25))
    交易中心[大类][小类序号].内容[编号].日涨跌幅 = 交易中心[大类][小类序号].内容[编号].日涨跌幅 + 0.25
    交易中心[大类][小类序号].内容[编号].数量 = 交易中心[大类][小类序号].内容[编号].数量 - 1

    -- 保存到文件
    写出文件([[sql/交易中心.txt]], table.tostring(交易中心))

    发送数据(玩家数据[id].连接id,3911,商品列表)
end

function 系统商城:出售交易中心商品(id,内容)
    local 大类=内容.大类 + 0
    local 小类=内容.小类
    local 编号=内容.编号 + 0
    local 商品列表,小类序号=获取交易中心列表(大类,小类)
    local 选中商品 = 商品列表[编号]

    if 选中商品.日涨跌幅 <= -10.0 then
        发送数据(玩家数据[id].连接id, 7, "#y/该商品已经跌停了，无法出售")
        return
    end

    local 扣除成功
    if 大类 == 3 then
        扣除成功=玩家数据[id].道具:扣除道具数量(id,选中商品.名称,1)
    else
        扣除成功=玩家数据[id].道具:扣除道具数量(id,选中商品.名称,1,选中商品.参数一类型,选中商品.参数一,选中商品.参数二类型,选中商品.参数二)
    end

    if 扣除成功 then
        local 出售价格=math.floor(选中商品.价格*0.5)
        常规提示(id,"#Y出售成功，你获得了#R"..出售价格.."#Y两银子")
        玩家数据[id].角色:添加银子(出售价格,"交易中心出售",1)
        -- 更新价格和涨跌幅
        交易中心[大类][小类序号].内容[编号].价格 = math.floor(交易中心[大类][小类序号].内容[编号].价格 / (100+交易中心[大类][小类序号].内容[编号].日涨跌幅) * (100+交易中心[大类][小类序号].内容[编号].日涨跌幅 - 0.25))
        交易中心[大类][小类序号].内容[编号].日涨跌幅 = 交易中心[大类][小类序号].内容[编号].日涨跌幅 - 0.25
        交易中心[大类][小类序号].内容[编号].数量 = 交易中心[大类][小类序号].内容[编号].数量 + 1
        写出文件([[sql/交易中心.txt]], table.tostring(交易中心))
        刷新玩家货币(玩家数据[id].连接id,id)
    end

    发送数据(玩家数据[id].连接id,3911,商品列表)
end

function 系统商城:刷新交易中心数量()
    for 大类序号=1,#交易中心 do
        for 小类序号=1,#交易中心[大类序号] do
            for 商品序号=1,#交易中心[大类序号][小类序号].内容 do
                local 商品 = 交易中心[大类序号][小类序号].内容[商品序号]
                商品.日涨跌幅 = 0.0
                local 配置信息 = self:获取商品配置(商品.名称, 商品.参数一)
                if 配置信息 then
                    商品.数量 = 配置信息.数量 or 商品.数量
                    商品.价格 = 配置信息.价格 or 商品.价格
                else
                    if 商品.名称 == "百炼精铁" then
                        商品.数量 = 取百炼精铁数量(商品.参数一)
                        商品.价格 = 取百炼精铁价格(商品.参数一)
                    elseif 商品.名称 == "制造指南书" then
                        商品.数量 = 取制造指南书数量(商品.参数一)
                        商品.价格 = 取制造指南书价格(商品.参数一)
                    elseif 商品.名称 == "魔兽要诀" then
                        商品.数量 = 取低级魔兽要诀数量(商品.参数一)
                        商品.价格 = 取低级魔兽要诀价格(商品.参数一)
                    elseif 商品.名称 == "高级魔兽要诀" then
                        商品.数量 = 取高级魔兽要诀数量(商品.参数一)
                        商品.价格 = 取高级魔兽要诀价格(商品.参数一)
                    elseif 商品.名称 == "特效宝珠" or 商品.名称 == "特性宝珠" or 商品.名称 == "武器专属石" then
                        商品.数量 = 取杂货数量(商品.名称)
                        商品.价格 = 取杂货数量(商品.名称)
                    end
                end
            end
        end
    end
