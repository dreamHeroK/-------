function 初始化交易中心()
    local 装备相关={}
    交易中心={}
    if f函数.文件是否存在([[sql/交易中心.txt]])==false then
        写出文件([[sql/交易中心.txt]],table.tostring({}))
    else
        交易中心=table.loadstring(读入文件([[sql/交易中心.txt]]))
    end

    -- 如果交易中心为空，则初始化数据
    if #交易中心 == 0 then
        table.insert(装备相关,{类型="100装备",内容=取制造指南书组(100)})
        table.insert(装备相关,{类型="110装备",内容=取制造指南书组(110)})
        table.insert(装备相关,{类型="120装备",内容=取制造指南书组(120)})
        table.insert(装备相关,{类型="130装备",内容=取制造指南书组(130)})
        table.insert(装备相关,{类型="140装备",内容=取制造指南书组(140)})
        table.insert(装备相关,{类型="150装备",内容=取制造指南书组(150)})
        table.insert(交易中心,装备相关)

        local 兽决相关={}
        table.insert(兽决相关,{类型="低兽决",内容=取低级魔兽要诀组()})
        table.insert(兽决相关,{类型="高兽决",内容=取高级魔兽要诀组()})
        table.insert(交易中心,兽决相关)

        local 杂货相关={}
        table.insert(杂货相关,{类型="杂货",内容=取杂货组()})
        table.insert(交易中心,杂货相关)

        -- 保存初始化数据
        写出文件([[sql/交易中心.txt]], table.tostring(交易中心))
    end
    __S服务:输出("初始化交易中心成功")
end

