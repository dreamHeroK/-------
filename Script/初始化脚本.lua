function 刷新排行榜(id)
    -- 通用排行榜更新函数（提取重复逻辑）
    local function 更新排行榜(排行名称, 分数获取函数, 是否需要外观数据, 是否敏感账号)
        local 符合 = true
        local 目标排行 = 排行榜数据[排行名称]
        local 玩家分数 = 分数获取函数()
        if 玩家分数 <= 0 then
          return
        end
        -- 1. 检查玩家是否已在排行榜，处理数据更新
        for k = 1, #目标排行 do
            local 排行项 = 目标排行[k]
            local 排行ID = 是否敏感账号 and 排行项.id or 排行项.id
            local 当前ID = 是否敏感账号 and (string.sub(玩家数据[id].账号, 1, 4) .. "***") or id
            if 排行ID == 当前ID then
                if 排行项.分数 == 玩家分数 then
                    符合 = false  -- 数据无变化，无需更新
                else
                    table.remove(目标排行, k)  -- 数据变化，移除旧记录
                end
                break
            end
        end

        if 符合 then
            local 排行数据项 = {
                id = 是否敏感账号 and (string.sub(玩家数据[id].账号, 1, 4) .. "***") or id,
                分数 = 玩家分数,
                名称 = 玩家数据[id].角色.名称
            }

            -- 2. 按需收集外观数据（锦衣、武器、光环、副武器）
            if 是否需要外观数据 then
                -- 锦衣数据
                if 玩家数据[id].角色.锦衣[1] then
                    排行数据项.锦衣 = {
                        子类 = 玩家数据[id].道具.数据[玩家数据[id].角色.锦衣[1]].子类,
                        名称 = 玩家数据[id].道具.数据[玩家数据[id].角色.锦衣[1]].名称
                    }
                else
                    排行数据项.锦衣 = nil
                end

                -- 光环数据
                if 玩家数据[id].角色.锦衣[3] then
                    排行数据项.光环 = {
                        子类 = 玩家数据[id].道具.数据[玩家数据[id].角色.锦衣[3]].子类,
                        名称 = 玩家数据[id].道具.数据[玩家数据[id].角色.锦衣[3]].名称
                    }
                else
                    排行数据项.光环 = nil
                end

                -- 武器数据
                if 玩家数据[id].角色.装备[3] then
                    排行数据项.武器 = {
                        子类 = 玩家数据[id].道具.数据[玩家数据[id].角色.装备[3]].子类,
                        名称 = 玩家数据[id].道具.数据[玩家数据[id].角色.装备[3]].名称,
                        等级 = 玩家数据[id].道具.数据[玩家数据[id].角色.装备[3]].等级
                    }
                else
                    排行数据项.武器 = nil
                end


                -- 副武器数据（仅影精灵+特定子类）
                if 玩家数据[id].角色.模型 == "影精灵"
                    and 玩家数据[id].角色.装备 ~= nil
                    and 玩家数据[id].角色.装备[4] ~= nil
                    and 玩家数据[id].道具.数据[玩家数据[id].角色.装备[4]].子类 == 911 then
                    排行数据项.副武器 = {
                        子类 = 911,
                        名称 = 玩家数据[id].道具.数据[玩家数据[id].角色.装备[4]].名称,
                        等级 = 玩家数据[id].道具.数据[玩家数据[id].角色.装备[4]].等级
                    }
                else
                    排行数据项.副武器 = nil
                end

                -- 模型数据
                排行数据项.模型 = 玩家数据[id].角色.模型
            end

            -- 3. 添加新记录到排行榜
            table.insert(目标排行, 排行数据项)

            -- 4. 排序（降序）
            if #目标排行 >= 1 then
                table.sort(目标排行, function(a, b) return a.分数 > b.分数 end)
            end

            -- 5. 截取前10条记录
            if #目标排行 > 10 then
                for m = 11, #目标排行 do
                    table.remove(目标排行)
                end
            end
        end
    end

    -- ===================== 调用通用函数更新各排行榜 =====================
    -- 1. 玩家伤害排行
    更新排行榜("玩家伤害排行", function() return 玩家数据[id].角色.伤害 end, true, false)

    -- 2. 玩家灵力排行
    更新排行榜("玩家灵力排行", function() return 玩家数据[id].角色.灵力 end, true, false)

    -- 3. 玩家防御排行
    更新排行榜("玩家防御排行", function() return 玩家数据[id].角色.防御 end, true, false)

    -- 4. 玩家仙玉排行（特殊处理：敏感账号+无需外观数据）
    local 仙玉分数 = tonumber(取仙玉(id)) or 0
        更新排行榜("玩家仙玉排行", function() return 仙玉分数 end, false, true)

    -- 5. 玩家等级排行
    更新排行榜("玩家等级排行", function() return 玩家数据[id].角色.等级 end, true, false)

    -- 6. 玩家财富排行（银子+存银）
    更新排行榜("玩家财富排行", function() return 玩家数据[id].角色.银子 + 玩家数据[id].角色.存银 end, true, false)

    -- 7. 玩家气血排行（最大气血）
    更新排行榜("玩家气血排行", function() return 玩家数据[id].角色.最大气血 end, true, false)

    -- 8. 玩家速度排行
    更新排行榜("玩家速度排行", function() return 玩家数据[id].角色.速度 end, true, false)

    -- 写出文件（保持原有逻辑）
    写出文件([[sql/排行榜.txt]], table.tostring(排行榜数据))
end



function 藏宝阁更新()
  local 改变 = false
  for i,v in pairs(藏宝阁数据) do
    for n=1,#藏宝阁数据[i] do
      if 藏宝阁数据[i][n] ~= nil and os.time() > 藏宝阁数据[i][n].结束时间 then
        local id = 藏宝阁数据[i][n].所有者
        if i ~= "银两" and i ~= "召唤兽" and i ~= "角色" then
          if 寄存数据[id] == nil then
            寄存数据[id] = {[1]={类型="物品",物品=藏宝阁数据[i][n].物品}}
          else
            寄存数据[id][#寄存数据[id]+1] = {类型="物品",物品=藏宝阁数据[i][n].物品}
          end
        elseif i == "银两" then
          if 寄存数据[id] == nil then
            寄存数据[id] = {[1]={类型="银子",数额=藏宝阁数据[i][n].数额}}
          else
            寄存数据[id][#寄存数据[id]+1] = {类型="银子",数额=藏宝阁数据[i][n].数额}
          end
        elseif i == "召唤兽" then
          if 寄存数据[id] == nil then
            寄存数据[id] = {[1]={类型="召唤兽",召唤兽=藏宝阁数据[i][n].召唤兽}}
          else
            寄存数据[id][#寄存数据[id]+1] = {类型="召唤兽",召唤兽=藏宝阁数据[i][n].召唤兽}
          end
        elseif i == "角色" then
          local 角色信息 = table.loadstring(读入文件([[data/]]..藏宝阁数据.角色[n].角色信息.账号..[[/]]..藏宝阁数据.角色[n].所有者..[[/角色.txt]]))
          角色信息.藏宝阁出售 = nil
          写出文件([[data/]]..藏宝阁数据.角色[n].角色信息.账号..[[/]]..藏宝阁数据.角色[n].所有者..[[/角色.txt]],table.tostring(角色信息))
          角色信息 = nil
        end
        table.remove(藏宝阁数据[i],n)
        改变 = true
      end
    end
  end
  if 改变 then
    for i,v in pairs(观察藏宝阁数据) do
      if 玩家数据[i] ~= nil then
        发送数据(玩家数据[i].连接did,12205 , 藏宝阁数据)
      else
          玩家数据[i] = nil
      end
    end
  end
end

