function 获取__商店数据()
    local 临时数据=ReadExcel1("商店数据","")
    local 商品列表 = {}
    local itemCount = #临时数据
    for i = 1, itemCount do
        local item = {}
        for k,v in pairs(临时数据[i]) do
            for m = 1, 20 do
                if "物品"..m == k then
