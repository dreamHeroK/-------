function 助战处理类:加载助战列表(数字id)
	local 账号=玩家数据[数字id].角色.账号
	玩家数据[数字id].助战列表=table.loadstring(读入文件([[data/]]..账号..[[/信息.txt]]))
	for n=1,#玩家数据[数字id].助战列表 do
		if 玩家数据[数字id].助战列表[n]==数字id then
			table.remove(玩家数据[数字id].助战列表, n)
			break
		end
	end
end
