		-- ====== BUG修复：修罗咒反震死亡后中断后续流程 ======
		if 战斗数据.参战单位[编号].气血 <= 0 then
			战斗数据.战斗流程[#战斗数据.战斗流程].挨打方[1].伤害=结果.伤害
			战斗数据.战斗流程[#战斗数据.战斗流程].挨打方[1].伤害类型=结果.类型
			战斗数据.战斗流程[#战斗数据.战斗流程].返回=返回
			return
		end
		-- ===============================
	end

	战斗数据.战斗流程[#战斗数据.战斗流程].挨打方[1].伤害=结果.伤害
	战斗数据.战斗流程[#战斗数据.战斗流程].挨打方[1].伤害类型=结果.类型
	战斗数据.战斗流程[#战斗数据.战斗流程].返回=返回
end
