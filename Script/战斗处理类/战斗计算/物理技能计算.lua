	if 吸血==nil and 战斗数据.参战单位[编号].法术状态.移魂化骨 then
		吸血=战斗数据.参战单位[编号].法术状态.移魂化骨.吸血
	end
	if 吸血~=nil and 战斗数据.参战单位[目标].鬼魂==nil  then
		local 吸血伤害=math.floor(结果.伤害*吸血)
		if 吸血伤害<1 then
			吸血伤害=1
		end
		战斗数据:增加气血(编号,吸血伤害)
		战斗数据.战斗流程[#战斗数据.战斗流程].吸血伤害=吸血伤害
