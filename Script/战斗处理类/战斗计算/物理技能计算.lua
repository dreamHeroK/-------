				战斗数据.战斗流程[#战斗数据.战斗流程].返回 = true
				return
			end
		elseif 结果.类型~=2 then
			结果.伤害=qz(结果.伤害*1.5)
			if 战斗数据.参战单位[目标].玉砥柱 then
				结果.伤害 = 结果.伤害 * (1 - 战斗数据.参战单位[目标].玉砥柱)
			end
		end
	end
	结果.伤害=qz(结果.伤害)
	if 结果.伤害<1 or 战斗数据.参战单位[目标].物理免疫 then
		结果.伤害=1
	end

