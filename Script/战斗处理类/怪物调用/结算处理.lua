    if 玩家数据[数字id] and 玩家数据[数字id].角色 then
        玩家数据[数字id].角色:增加剧情点(2)
    end

    -- 5. 同步添加称谓
    同步队伍添加称谓(数字id, "生死劫·娑罗")

    -- 6. 同步增加潜力点
    同步队伍增加潜力(数字id, 25)

    -- 7. 给队长书铁奖励（只有队长有）
    if 玩家数据[数字id] and 玩家数据[数字id].道具 then
        玩家数据[数字id].道具:给予书铁(数字id, {15, 15})
    end

    -- 8. 广播消息（每个成员都播报）
    for i = 1, #id组 do
        local cyid = id组[i]
        if 玩家数据[cyid] and 玩家数据[cyid].角色 then
            广播消息({
                内容 = format("#S(大唐新闻快报)#R/%s#Y在#R凌霄宝殿#Y通过了生死劫的考验的脱胎换骨，" .. "#" .. random(1, 110),
                    玩家数据[cyid].角色.名称),
                频道 = "xt"
            })
        end
    end

    -- 9. 同步发送NPC对话
    同步队伍发送对话(数字id, "男人_玉帝", "玉皇大帝", "恭喜少侠渡过第九层生死劫完成渡劫")

    -- 10. 【关键修复】同步刷新客户端剧情显示
    同步刷新队伍剧情追踪(数字id)
end
