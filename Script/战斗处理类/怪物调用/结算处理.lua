            玩家数据[cyid].角色.潜力 = 玩家数据[cyid].角色.潜力 + 潜力值
        end
    end
end

function 同步队伍发送对话(数字id, 模型, 名称, 对话)
    local id组 = 取id组(数字id)
    for i = 1, #id组 do
        local cyid = id组[i]
