function 初始化生死劫数据(数字id)
    if 玩家数据[数字id] == nil or 玩家数据[数字id].角色 == nil then
        return false
    end
    if 玩家数据[数字id].角色.历劫 == nil then
        玩家数据[数字id].角色.历劫 = {}
    end
    if 玩家数据[数字id].角色.历劫.生死劫 == nil then
        玩家数据[数字id].角色.历劫.生死劫 = {
            [1]=false,[2]=false,[3]=false,[4]=false,
            [5]=false,[6]=false,[7]=false,[8]=false,[9]=false
        }
    end
    return true
end

function 校验个人生死劫条件(数字id, 劫数)
    if 玩家数据[数字id] == nil or 玩家数据[数字id].角色 == nil then
        return false, "玩家数据异常"
    end

    if 玩家数据[数字id].角色.等级 < 120 then
        return false, "等级不足120级"
    end

    if 玩家数据[数字id].角色.历劫 == nil or 玩家数据[数字id].角色.历劫.飞升 ~= true then
        return false, "未飞升"
    end

    初始化生死劫数据(数字id)

    for i = 1, 劫数 - 1 do
        if 玩家数据[数字id].角色.历劫.生死劫[i] ~= true then
            return false, "前置劫数未完成"
        end
    end

    if 玩家数据[数字id].角色.历劫.生死劫[劫数] == true then
        return false, "此劫已完成"
    end

    return true, ""
end

function 校验队伍生死劫进度(数字id, 劫数)
    if 玩家数据[数字id].队伍 == 0 then
        local ok, reason = 校验个人生死劫条件(数字id, 劫数)
        return ok, reason
    end

    local 队伍id = 玩家数据[数字id].队伍
    if 队伍数据[队伍id] == nil then
        return true, ""
    end

    local ok, reason = 校验个人生死劫条件(数字id, 劫数)
    if not ok then
        return false, "你自己" .. reason
    end

    for n = 2, #队伍数据[队伍id].成员数据 do
        local 队员id = 队伍数据[队伍id].成员数据[n]
        local ok2, reason2 = 校验个人生死劫条件(队员id, 劫数)
        if not ok2 then
            return false, 玩家数据[队员id].角色.名称 .. reason2
        end
    end

    return true, ""
end

function 初始化队伍生死劫数据(数字id)
    if 玩家数据[数字id].队伍 == 0 then
        初始化生死劫数据(数字id)
        return
    end
    local 队伍id = 玩家数据[数字id].队伍
    if 队伍数据[队伍id] == nil then
        return
    end
    for n = 1, #队伍数据[队伍id].成员数据 do
        local 队员id = 队伍数据[队伍id].成员数据[n]
        初始化生死劫数据(队员id)
    end
end

function 处理生死劫NPC进入(数字id, 劫数)
