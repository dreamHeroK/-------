function 同步队伍完成生死劫(数字id, 劫数)
    local id组 = 取id组(数字id)

    for i = 1, #id组 do
        local cyid = id组[i]

        if 玩家数据[cyid] and 玩家数据[cyid].角色 then
            初始化生死劫数据(cyid)

            local 历劫 = 玩家数据[cyid].角色.历劫
            local 可以同步 = true

            if 历劫.生死劫[劫数] == true then
                可以同步 = false
            else
                for j = 1, 劫数 - 1 do
                    if 历劫.生死劫[j] ~= true then
                        可以同步 = false
                        break
                    end
                end
            end

            if 可以同步 then
                历劫.生死劫[劫数] = true
                常规提示(cyid, "#G恭喜你成功渡过了生死劫第" .. 劫数 .. "劫·" .. (生死劫名称[劫数] or "") .. "！")

                if 玩家数据[cyid].角色.刷新任务跟踪 then
                    玩家数据[cyid].角色:刷新任务跟踪()
                end
            end
        end
    end
end

function 同步队伍剧情进度(数字id, 劫数)
    local id组 = 取id组(数字id)

    for i = 1, #id组 do
        local cyid = id组[i]

        if 玩家数据[cyid] and 玩家数据[cyid].角色 and 玩家数据[cyid].角色.剧情 then
            local 当前剧情 = 玩家数据[cyid].角色.剧情
            local 下一进度 = 劫数 + 2
            if 劫数 == 9 then
                下一进度 = 11
            end

            if 当前剧情.主线 == 9 then

                玩家数据[cyid].角色.剧情 = {主线=9, 编号=3, 地图=1112, 进度=下一进度, 附加={战斗=1}}
            elseif 当前剧情.主线 == 10 then

                if 劫数 == 1 then
                    下一进度 = 2
                elseif 劫数 == 9 then
                    下一进度 = 11
                end
                玩家数据[cyid].角色.剧情 = {主线=10, 编号=21, 地图=1112, 进度=下一进度, 附加={战斗=1}}
            end
        end
    end
end

function 同步刷新队伍剧情追踪(数字id)
    local id组 = 取id组(数字id)
    for i = 1, #id组 do
        local cyid = id组[i]
        刷新剧情追踪(cyid)
    end
end


function 安全添加称谓(数字id, 称谓名)
    if 玩家数据[数字id] == nil or 玩家数据[数字id].角色 == nil then
        return
    end
    for i = 1, #玩家数据[数字id].角色.称谓 do
        if 玩家数据[数字id].角色.称谓[i] == 称谓名 then
            return
        end
    end
    玩家数据[数字id].角色.称谓[#玩家数据[数字id].角色.称谓 + 1] = 称谓名
end

function 同步队伍添加称谓(数字id, 称谓名)
    local id组 = 取id组(数字id)
    for i = 1, #id组 do
        安全添加称谓(id组[i], 称谓名)
    end
end


function 同步队伍增加潜力(数字id, 潜力值)
    local id组 = 取id组(数字id)
    for i = 1, #id组 do
        local cyid = id组[i]
